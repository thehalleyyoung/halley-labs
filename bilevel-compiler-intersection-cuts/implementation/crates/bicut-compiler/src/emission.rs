/// Writes CPLEX-style LP format files.
pub struct LpWriter {
    config: EmissionConfig,
}

impl LpWriter {
    pub fn new(config: EmissionConfig) -> Self {
        Self { config }
    }

    pub fn write(&self, milp: &MilpProblem) -> Result<EmissionResult, CompilerError> {
        if milp.variables.is_empty() {
            return Err(CompilerError::InvalidProblem(
                "Cannot emit LP for a problem with zero variables".into(),
            ));
        }
        let mut warnings: Vec<String> = Vec::new();
        let nv = milp.variables.len();
        let nc = milp.constraints.len();
        let mut buf = String::with_capacity(nv * 40 + nc * 60);
        if self.config.include_comments {
            writeln!(buf, "\\ Generated by bicut-compiler emission module").unwrap();
            writeln!(buf, "\\ Variables: {}  Constraints: {}", nv, nc).unwrap();
        }
        buf.push_str(&self.write_objective(milp));
        buf.push_str(&self.write_constraints(milp));
        buf.push_str(&self.write_bounds(milp));
        buf.push_str(&self.write_integers(milp));
        let sos = self.write_sos(milp);
        if !sos.is_empty() {
            buf.push_str(&sos);
        }
        buf.push_str("End\n");
        for i in 0..nv {
            let raw = resolve_var_name(milp, i);
            if raw.len() > 255 {
                warnings.push(format!(
                    "Variable name '{}...' exceeds 255 chars",
                    &raw[..40]
                ));
            }
        }
        let size = buf.len();
        Ok(EmissionResult {
            content: buf,
            format: OutputFormat::Lp,
            num_vars_written: nv,
            num_constraints_written: nc,
            file_size_bytes: size,
            warnings,
        })
    }

    pub fn write_objective(&self, milp: &MilpProblem) -> String {
        let kw = match milp.sense {
            OptDirection::Minimize => "Minimize",
            OptDirection::Maximize => "Maximize",
        };
        let mut buf = format!("{}\n  obj: ", kw);
        let mut first = true;
        for (col, var) in milp.variables.iter().enumerate() {
            let c = var.obj_coeff;
            if c.abs() < 1e-30 {
                continue;
            }
            buf.push_str(&Self::format_term(c, &resolve_var_name(milp, col), first));
            first = false;
        }
        if first {
            buf.push('0');
        }
        buf.push('\n');
        buf
    }

    pub fn write_constraints(&self, milp: &MilpProblem) -> String {
        if milp.constraints.is_empty() {
            return String::new();
        }
        let mut buf = String::from("Subject To\n");
        for (row, con) in milp.constraints.iter().enumerate() {
            let cname = resolve_constraint_name(milp, row);
            let lhs = self.format_constraint_lhs(row, milp);
            let sense = match con.sense {
                ConstraintSense::Le => "<=",
                ConstraintSense::Ge => ">=",
                ConstraintSense::Eq => "=",
            };
            let rhs = con.rhs;
            writeln!(buf, "  {}: {} {} {}", cname, lhs, sense, self.fmt_val(rhs)).unwrap();
        }
        buf
    }

    pub fn write_bounds(&self, milp: &MilpProblem) -> String {
        let mut buf = String::from("Bounds\n");
        for (col, var) in milp.variables.iter().enumerate() {
            let vn = resolve_var_name(milp, col);
            let vt = var.var_type;
            let lb = var.lower_bound;
            let ub = var.upper_bound;
            if vt == VarType::Binary {
                writeln!(buf, "  0 <= {} <= 1", vn).unwrap();
            } else if lb <= -1e20 && ub >= 1e20 {
                writeln!(buf, "  {} free", vn).unwrap();
            } else if ub >= 1e20 {
                writeln!(buf, "  {} <= {} <= +inf", self.fmt_val(lb), vn).unwrap();
            } else if lb <= -1e20 {
                writeln!(buf, "  -inf <= {} <= {}", vn, self.fmt_val(ub)).unwrap();
            } else {
                writeln!(
                    buf,
                    "  {} <= {} <= {}",
                    self.fmt_val(lb),
                    vn,
                    self.fmt_val(ub)
                )
                .unwrap();
            }
        }
        buf
    }

    pub fn write_integers(&self, milp: &MilpProblem) -> String {
        let mut gen: Vec<String> = Vec::new();
        let mut bin: Vec<String> = Vec::new();
        for (col, var) in milp.variables.iter().enumerate() {
            match var.var_type {
                VarType::Integer => gen.push(resolve_var_name(milp, col)),
                VarType::Binary => bin.push(resolve_var_name(milp, col)),
                _ => {}
            }
        }
        let mut buf = String::new();
        if !gen.is_empty() {
            buf.push_str("General\n");
            for chunk in gen.chunks(10) {
                writeln!(buf, "  {}", chunk.join(" ")).unwrap();
            }
        }
        if !bin.is_empty() {
            buf.push_str("Binary\n");
            for chunk in bin.chunks(10) {
                writeln!(buf, "  {}", chunk.join(" ")).unwrap();
            }
        }
        buf
    }

    pub fn write_sos(&self, milp: &MilpProblem) -> String {
        if milp.sos1_sets.is_empty() {
            return String::new();
        }
        let mut buf = String::from("SOS\n");
        for sos in &milp.sos1_sets {
            let label = if sos.sos_type == 1 { "S1" } else { "S2" };
            let name = if sos.name.is_empty() {
                "sos"
            } else {
                &sos.name
            };
            writeln!(buf, "  {} :: {}", name, label).unwrap();
            for (j, &vi) in sos.members.iter().enumerate() {
                let w = sos.weights.get(j).copied().unwrap_or(j as f64 + 1.0);
                writeln!(
                    buf,
                    "    {} : {}",
                    resolve_var_name(milp, vi),
                    self.fmt_val(w)
                )
                .unwrap();
            }
        }
        buf
    }

    /// Build the LHS expression string for a constraint row, e.g. `2 x1 + 3 x2 - x3`.
    pub fn format_constraint_lhs(&self, row: usize, milp: &MilpProblem) -> String {
        let entries = milp.constraints.get(row).map(|c| &c.coeffs);
        let mut first = true;
        let mut result = String::new();
        if let Some(entries) = entries {
            for &(col, coeff) in entries {
                if coeff.abs() < 1e-30 && self.config.compress_zeros {
                    continue;
                }
                result.push_str(&Self::format_term(
                    coeff,
                    &resolve_var_name(milp, col),
                    first,
                ));
                first = false;
            }
        }
        if result.is_empty() {
            "0".into()
        } else {
            result
        }
    }

    /// Format a single coefficient x variable term for LP output.
    pub fn format_term(coeff: f64, var_name: &str, is_first: bool) -> String {
        let abs = coeff.abs();
        let neg = coeff < 0.0;
        let cs = if (abs - 1.0).abs() < 1e-15 {
            String::new()
        } else {
            let r = format!("{}", abs);
            let t = if r.contains('.') {
                r.trim_end_matches('0').trim_end_matches('.').to_string()
            } else {
                r
            };
            format!("{} ", t)
        };
        if is_first {
            if neg {
                format!("- {}{}", cs, var_name)
            } else {
                format!("{}{}", cs, var_name)
            }
        } else if neg {
            format!(" - {}{}", cs, var_name)
        } else {
            format!(" + {}{}", cs, var_name)
        }
    }

    fn fmt_val(&self, val: f64) -> String {
        if val == 0.0 {
            return "0".into();
        }
        let s = format!("{:.*}", self.config.precision, val);
        if s.contains('.') {
            let t = s.trim_end_matches('0').trim_end_matches('.');
            if t.is_empty() || t == "-" {
                "0".into()
            } else {
                t.into()
            }
        } else {
            s
        }
    }
}

// ════════════════════════════════════════════════════════════════════
//  Top-level dispatch
// ════════════════════════════════════════════════════════════════════

/// Emit a MILP problem to the format specified in `config`.
pub fn emit(milp: &MilpProblem, config: &EmissionConfig) -> Result<EmissionResult, CompilerError> {
    match config.format {
        OutputFormat::Mps | OutputFormat::FreeMps => MpsWriter::new(config.clone()).write(milp),
        OutputFormat::Lp => LpWriter::new(config.clone()).write(milp),
        OutputFormat::CipScip => Err(CompilerError::InvalidProblem(
            "CIP (SCIP) format emission is not yet implemented".into(),
        )),
        OutputFormat::GrbGurobi => Err(CompilerError::InvalidProblem(
            "GRB (Gurobi) native format emission is not yet implemented".into(),
        )),
    }
}

