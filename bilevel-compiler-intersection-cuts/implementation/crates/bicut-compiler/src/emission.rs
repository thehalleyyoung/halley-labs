/// Writes standard and free-format MPS files.
pub struct MpsWriter {
    config: EmissionConfig,
}

impl MpsWriter {
    pub fn new(config: EmissionConfig) -> Self {
        Self { config }
    }

    pub fn write(&self, milp: &MilpProblem) -> Result<EmissionResult, CompilerError> {
        if milp.variables.is_empty() {
            return Err(CompilerError::InvalidProblem(
                "Cannot emit MPS for a problem with zero variables".into(),
            ));
        }
        let mut warnings: Vec<String> = Vec::new();
        let nv = milp.variables.len();
        let nc = milp.constraints.len();
        let mut buf = String::with_capacity(nv * 80 + nc * 80);
        if self.config.include_comments {
            writeln!(buf, "* Generated by bicut-compiler emission module").unwrap();
            writeln!(buf, "* Variables: {}  Constraints: {}", nv, nc).unwrap();
        }
        buf.push_str(&self.write_name_section(milp));
        buf.push_str(&self.write_rows_section(milp));
        buf.push_str(&self.write_columns_section(milp));
        buf.push_str(&self.write_rhs_section(milp));
        let ranges = self.write_ranges_section(milp);
        if !ranges.is_empty() {
            buf.push_str(&ranges);
        }
        buf.push_str(&self.write_bounds_section(milp));
        let sos = self.write_sos_section(milp);
        if !sos.is_empty() {
            buf.push_str(&sos);
        }
        buf.push_str("ENDATA\n");
        for i in 0..nv {
            let raw = resolve_var_name(milp, i);
            if raw.len() > self.config.max_name_length && self.config.use_fixed_format_mps {
                warnings.push(format!(
                    "Variable name '{}' truncated to {} chars",
                    raw, self.config.max_name_length
                ));
            }
        }
        let size = buf.len();
        Ok(EmissionResult {
            content: buf,
            format: self.config.format,
            num_vars_written: nv,
            num_constraints_written: nc,
            file_size_bytes: size,
            warnings,
        })
    }

    pub fn write_name_section(&self, milp: &MilpProblem) -> String {
        let name = if milp.name.is_empty() {
            "UNNAMED"
        } else {
            &milp.name
        };
        format!("NAME          {}\n", name)
    }

    pub fn write_rows_section(&self, milp: &MilpProblem) -> String {
        let mut buf = String::from("ROWS\n");
        let sep = if self.config.use_fixed_format_mps {
            "  "
        } else {
            " "
        };
        writeln!(buf, " N{}OBJ", sep).unwrap();
        for (i, con) in milp.constraints.iter().enumerate() {
            let rt = match con.sense {
                ConstraintSense::Le => 'L',
                ConstraintSense::Ge => 'G',
                ConstraintSense::Eq => 'E',
            };
            writeln!(
                buf,
                " {}{}{}",
                rt,
                sep,
                self.sanitize_name(&resolve_constraint_name(milp, i))
            )
            .unwrap();
        }
        buf
    }

    pub fn write_columns_section(&self, milp: &MilpProblem) -> String {
        let mut buf = String::from("COLUMNS\n");
        let (mut in_int, mut mk) = (false, 0usize);
        for (col, var) in milp.variables.iter().enumerate() {
            let vname = self.sanitize_name(&resolve_var_name(milp, col));
            let is_int = matches!(var.var_type, VarType::Integer | VarType::Binary);
            if is_int && !in_int {
                mk += 1;
                self.marker(&mut buf, mk, "INTORG");
                in_int = true;
            }
            if !is_int && in_int {
                mk += 1;
                self.marker(&mut buf, mk, "INTEND");
                in_int = false;
            }
            let mut named: Vec<(String, f64)> = Vec::new();
            let obj = var.obj_coeff;
            if !self.skip(obj) {
                named.push(("OBJ".into(), obj));
            }
            for (row, val) in get_column_entries(milp, col) {
                if !self.skip(val) {
                    named.push((self.sanitize_name(&resolve_constraint_name(milp, row)), val));
                }
            }
            let mut idx = 0;
            while idx < named.len() {
                if self.config.use_fixed_format_mps && idx + 1 < named.len() {
                    writeln!(
                        buf,
                        "    {:8}  {:8}  {:>12}   {:8}  {:>12}",
                        vname,
                        named[idx].0,
                        self.format_value(named[idx].1),
                        named[idx + 1].0,
                        self.format_value(named[idx + 1].1)
                    )
                    .unwrap();
                    idx += 2;
                } else if self.config.use_fixed_format_mps {
                    writeln!(
                        buf,
                        "    {:8}  {:8}  {:>12}",
                        vname,
                        named[idx].0,
                        self.format_value(named[idx].1)
                    )
                    .unwrap();
                    idx += 1;
                } else {
                    writeln!(
                        buf,
                        "    {} {} {}",
                        vname,
                        named[idx].0,
                        self.format_value(named[idx].1)
                    )
                    .unwrap();
                    idx += 1;
                }
            }
        }
        if in_int {
            mk += 1;
            self.marker(&mut buf, mk, "INTEND");
        }
        buf
    }

    fn marker(&self, buf: &mut String, id: usize, kind: &str) {
        let mid = format!("M{:07}", id);
        if self.config.use_fixed_format_mps {
            writeln!(buf, "    {:8}  'MARKER'                 '{}'", mid, kind).unwrap();
        } else {
            writeln!(buf, "    {} 'MARKER' '{}'", mid, kind).unwrap();
        }
    }

    pub fn write_rhs_section(&self, milp: &MilpProblem) -> String {
        let mut buf = String::from("RHS\n");
        for (i, con) in milp.constraints.iter().enumerate() {
            let rhs = con.rhs;
            if self.config.compress_zeros && rhs.abs() < 1e-30 {
                continue;
            }
            let cn = self.sanitize_name(&resolve_constraint_name(milp, i));
            if self.config.use_fixed_format_mps {
                writeln!(
                    buf,
                    "    {:8}  {:8}  {:>12}",
                    "RHS1",
                    cn,
                    self.format_value(rhs)
                )
                .unwrap();
            } else {
                writeln!(buf, "    RHS1 {} {}", cn, self.format_value(rhs)).unwrap();
            }
        }
        buf
    }

    pub fn write_ranges_section(&self, milp: &MilpProblem) -> String {
        let mut buf = String::from("RANGES\n");
        let mut any = false;
        for (i, con) in milp.constraints.iter().enumerate() {
            let rv = con.range.unwrap_or(0.0);
            if rv.abs() < 1e-30 {
                continue;
            }
            any = true;
            let cn = self.sanitize_name(&resolve_constraint_name(milp, i));
            if self.config.use_fixed_format_mps {
                writeln!(
                    buf,
                    "    {:8}  {:8}  {:>12}",
                    "RNG1",
                    cn,
                    self.format_value(rv)
                )
                .unwrap();
            } else {
                writeln!(buf, "    RNG1 {} {}", cn, self.format_value(rv)).unwrap();
            }
        }
        if any {
            buf
        } else {
            String::new()
        }
    }

    pub fn write_bounds_section(&self, milp: &MilpProblem) -> String {
        let mut buf = String::from("BOUNDS\n");
        for (col, var) in milp.variables.iter().enumerate() {
            let vn = self.sanitize_name(&resolve_var_name(milp, col));
            let vt = var.var_type;
            let lb = var.lower_bound;
            let ub = var.upper_bound;
            if vt == VarType::Binary {
                self.bnd(&mut buf, "BV", &vn, None);
            } else if lb <= -1e20 && ub >= 1e20 {
                self.bnd(&mut buf, "FR", &vn, None);
            } else if (ub - lb).abs() < 1e-10 {
                self.bnd(&mut buf, "FX", &vn, Some(lb));
            } else {
                if lb <= -1e20 {
                    self.bnd(&mut buf, "MI", &vn, None);
                } else if (lb - 0.0).abs() > 1e-30 {
                    self.bnd(&mut buf, "LO", &vn, Some(lb));
                }
                if ub < 1e20 {
                    self.bnd(&mut buf, "UP", &vn, Some(ub));
                }
            }
        }
        buf
    }

    fn bnd(&self, buf: &mut String, kind: &str, var: &str, val: Option<f64>) {
        match (val, self.config.use_fixed_format_mps) {
            (Some(v), true) => writeln!(
                buf,
                " {:2} {:8}  {:8}  {:>12}",
                kind,
                "BND1",
                var,
                self.format_value(v)
            )
            .unwrap(),
            (Some(v), false) => {
                writeln!(buf, " {} BND1 {} {}", kind, var, self.format_value(v)).unwrap()
            }
            (None, true) => writeln!(buf, " {:2} {:8}  {:8}", kind, "BND1", var).unwrap(),
            (None, false) => writeln!(buf, " {} BND1 {}", kind, var).unwrap(),
        }
    }

    pub fn write_sos_section(&self, milp: &MilpProblem) -> String {
        if milp.sos1_sets.is_empty() {
            return String::new();
        }
        let mut buf = String::from("SOS\n");
        for sos in &milp.sos1_sets {
            let label = if sos.sos_type == 1 { "S1" } else { "S2" };
            let name = if sos.name.is_empty() {
                "SOS".to_string()
            } else {
                self.sanitize_name(&sos.name)
            };
            writeln!(buf, " {} {}", label, name).unwrap();
            for (j, &vi) in sos.members.iter().enumerate() {
                let w = sos.weights.get(j).copied().unwrap_or(j as f64 + 1.0);
                let vn = self.sanitize_name(&resolve_var_name(milp, vi));
                if self.config.use_fixed_format_mps {
                    writeln!(buf, "    {:8}  {:>12}", vn, self.format_value(w)).unwrap();
                } else {
                    writeln!(buf, "    {} {}", vn, self.format_value(w)).unwrap();
                }
            }
        }
        buf
    }

    /// Format a floating-point value with configured precision.
    pub fn format_value(&self, val: f64) -> String {
        if val == 0.0 {
            return "0".into();
        }
        let abs = val.abs();
        if abs >= 1e10 || (abs < 1e-4 && abs > 0.0) {
            format!("{:.*e}", self.config.precision, val)
        } else {
            let s = format!("{:.*}", self.config.precision, val);
            if s.contains('.') {
                let t = s.trim_end_matches('0').trim_end_matches('.');
                if t.is_empty() || t == "-" {
                    "0".into()
                } else {
                    t.into()
                }
            } else {
                s
            }
        }
    }

    /// Ensure a name is valid for MPS: replace invalid chars, truncate to max length.
    pub fn sanitize_name(&self, name: &str) -> String {
        let cleaned: String = name
            .chars()
            .map(|c| {
                if c.is_ascii_alphanumeric() || c == '_' || c == '.' {
                    c
                } else {
                    '_'
                }
            })
            .collect();
        if cleaned.is_empty() {
            return "_".into();
        }
        if cleaned.len() > self.config.max_name_length {
            cleaned[..self.config.max_name_length].to_string()
        } else {
            cleaned
        }
    }

    fn skip(&self, val: f64) -> bool {
        self.config.compress_zeros && val.abs() < 1e-30
    }
}

