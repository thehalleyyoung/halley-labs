#[cfg(test)]
mod tests {
    use super::*;

    /// min 2x0 + 3x1 + x2
    /// s.t. x0 + x1       <= 10  (c0)
    ///      x0      + 2x2 >= 5   (c1)
    ///      x0 + x1 +  x2  = 8   (c2)
    /// x0 continuous [0,100], x1 binary, x2 integer [0,50]
    fn make_milp() -> MilpProblem {
        use crate::pipeline::{MilpConstraint, MilpVariable};

        let mut milp = MilpProblem::new("test_problem");
        milp.sense = OptDirection::Minimize;

        // Variables
        let mut x0 = MilpVariable::continuous("x0", 0.0, 100.0);
        x0.obj_coeff = 2.0;
        milp.add_variable(x0);

        let mut x1 = MilpVariable::binary("x1");
        x1.obj_coeff = 3.0;
        milp.add_variable(x1);

        let mut x2 = MilpVariable {
            name: "x2".into(),
            lower_bound: 0.0,
            upper_bound: 50.0,
            obj_coeff: 1.0,
            var_type: VarType::Integer,
        };
        milp.add_variable(x2);

        // Constraints
        let mut c0 = MilpConstraint::new("c0", ConstraintSense::Le, 10.0);
        c0.add_term(0, 1.0);
        c0.add_term(1, 1.0);
        milp.add_constraint(c0);

        let mut c1 = MilpConstraint::new("c1", ConstraintSense::Ge, 5.0);
        c1.add_term(0, 1.0);
        c1.add_term(2, 2.0);
        milp.add_constraint(c1);

        let mut c2 = MilpConstraint::new("c2", ConstraintSense::Eq, 8.0);
        c2.add_term(0, 1.0);
        c2.add_term(1, 1.0);
        c2.add_term(2, 1.0);
        milp.add_constraint(c2);

        milp
    }

    fn cfg(format: OutputFormat) -> EmissionConfig {
        EmissionConfig {
            format,
            precision: 6,
            use_fixed_format_mps: format == OutputFormat::Mps,
            max_name_length: 8,
            include_comments: false,
            compress_zeros: true,
        }
    }

    #[test]
    fn test_mps_name_section() {
        let w = MpsWriter::new(cfg(OutputFormat::Mps));
        let s = w.write_name_section(&make_milp());
        assert!(s.starts_with("NAME"));
        assert!(s.contains("test_problem"));
    }

    #[test]
    fn test_mps_rows_section_format() {
        let w = MpsWriter::new(cfg(OutputFormat::Mps));
        let s = w.write_rows_section(&make_milp());
        assert!(s.starts_with("ROWS\n"));
        assert!(s.contains(" N"));
        assert!(s.contains("OBJ"));
        assert!(s.contains(" L") && s.contains("c0"));
        assert!(s.contains(" G") && s.contains("c1"));
        assert!(s.contains(" E") && s.contains("c2"));
    }

    #[test]
    fn test_mps_columns_with_integers() {
        let w = MpsWriter::new(cfg(OutputFormat::Mps));
        let s = w.write_columns_section(&make_milp());
        assert!(s.starts_with("COLUMNS\n"));
        assert!(s.contains("INTORG"));
        assert!(s.contains("INTEND"));
        assert!(s.contains("x0") && s.contains("x1") && s.contains("x2"));
    }

    #[test]
    fn test_mps_bounds_bv_lo_up_fr() {
        use crate::pipeline::MilpVariable;

        let mut m = make_milp();
        m.add_variable(MilpVariable::continuous("x3", -1e21, 1e21));
        let w = MpsWriter::new(cfg(OutputFormat::Mps));
        let s = w.write_bounds_section(&m);
        assert!(s.starts_with("BOUNDS\n"));
        assert!(s.contains("BV") && s.contains("x1"));
        assert!(s.contains("UP"));
        assert!(s.contains("FR") && s.contains("x3"));
    }

    #[test]
    fn test_lp_objective_writing() {
        let w = LpWriter::new(cfg(OutputFormat::Lp));
        let s = w.write_objective(&make_milp());
        assert!(s.starts_with("Minimize\n"));
        assert!(s.contains("obj:"));
        assert!(s.contains("2 x0") && s.contains("3 x1") && s.contains("x2"));
    }

    #[test]
    fn test_lp_constraint_format() {
        let w = LpWriter::new(cfg(OutputFormat::Lp));
        let s = w.write_constraints(&make_milp());
        assert!(s.starts_with("Subject To\n"));
        assert!(s.contains("c0:") && s.contains("<=") && s.contains("10"));
        assert!(s.contains("c1:") && s.contains(">=") && s.contains("5"));
        assert!(s.contains("c2:") && s.contains("= 8"));
    }

    #[test]
    fn test_lp_bounds_section() {
        let w = LpWriter::new(cfg(OutputFormat::Lp));
        let s = w.write_bounds(&make_milp());
        assert!(s.starts_with("Bounds\n"));
        assert!(s.contains("0 <= x1 <= 1"));
        assert!(s.contains("x0") && s.contains("100"));
    }

    #[test]
    fn test_full_mps_roundtrip() {
        let r = emit(&make_milp(), &cfg(OutputFormat::Mps)).unwrap();
        assert_eq!(r.format, OutputFormat::Mps);
        assert_eq!(r.num_vars_written, 3);
        assert_eq!(r.num_constraints_written, 3);
        assert!(r.file_size_bytes > 0);
        let c = &r.content;
        let positions: Vec<usize> = ["NAME", "ROWS", "COLUMNS", "RHS\n", "BOUNDS", "ENDATA"]
            .iter()
            .map(|k| c.find(k).unwrap())
            .collect();
        for w in positions.windows(2) {
            assert!(w[0] < w[1]);
        }
    }

    #[test]
    fn test_full_lp_roundtrip() {
        let r = emit(&make_milp(), &cfg(OutputFormat::Lp)).unwrap();
        assert_eq!(r.format, OutputFormat::Lp);
        assert_eq!(r.num_vars_written, 3);
        let c = &r.content;
        let positions: Vec<usize> = ["Minimize", "Subject To", "Bounds", "End"]
            .iter()
            .map(|k| c.find(k).unwrap())
            .collect();
        for w in positions.windows(2) {
            assert!(w[0] < w[1]);
        }
        assert!(c.contains("Binary") && c.contains("General"));
    }

    #[test]
    fn test_value_formatting() {
        let w = MpsWriter::new(cfg(OutputFormat::Mps));
        assert_eq!(w.format_value(0.0), "0");
        assert_eq!(w.format_value(1.0), "1");
        assert_eq!(w.format_value(-3.5), "-3.5");
        assert_eq!(w.format_value(100.0), "100");
        assert!(w.format_value(1e-8).contains('e'));
        assert!(w.format_value(1e15).contains('e'));
    }

    #[test]
    fn test_sanitize_name() {
        let c = EmissionConfig {
            max_name_length: 8,
            ..cfg(OutputFormat::Mps)
        };
        let w = MpsWriter::new(c);
        assert_eq!(w.sanitize_name("x0"), "x0");
        assert_eq!(w.sanitize_name("very_long_variable_name"), "very_lon");
        assert_eq!(w.sanitize_name("has space"), "has_spac");
        assert_eq!(w.sanitize_name("a+b-c"), "a_b_c");
        assert_eq!(w.sanitize_name(""), "_");
    }

    #[test]
    fn test_format_term() {
        assert_eq!(LpWriter::format_term(2.0, "x1", true), "2 x1");
        assert_eq!(LpWriter::format_term(-3.0, "x2", true), "- 3 x2");
        assert_eq!(LpWriter::format_term(1.0, "x3", true), "x3");
        assert_eq!(LpWriter::format_term(-1.0, "x4", true), "- x4");
        assert_eq!(LpWriter::format_term(5.0, "y", false), " + 5 y");
        assert_eq!(LpWriter::format_term(-7.0, "z", false), " - 7 z");
        assert_eq!(LpWriter::format_term(1.0, "w", false), " + w");
    }

    #[test]
    fn test_get_column_entries() {
        let m = make_milp();
        assert_eq!(
            get_column_entries(&m, 0),
            vec![(0, 1.0), (1, 1.0), (2, 1.0)]
        );
        assert_eq!(get_column_entries(&m, 2), vec![(1, 2.0), (2, 1.0)]);
    }

    #[test]
    fn test_empty_problem_errors() {
        let empty = MilpProblem::new("");
        assert!(emit(&empty, &cfg(OutputFormat::Mps)).is_err());
        assert!(emit(&empty, &cfg(OutputFormat::Lp)).is_err());
    }

    #[test]
    fn test_sos_section_mps() {
        let mut m = make_milp();
        m.sos1_sets.push(Sos1Set {
            name: "sos_set1".into(),
            sos_type: 1,
            members: vec![0, 1],
            weights: vec![1.0, 2.0],
        });
        let w = MpsWriter::new(cfg(OutputFormat::Mps));
        let s = w.write_sos_section(&m);
        assert!(s.starts_with("SOS\n"));
        assert!(s.contains("S1") && s.contains("sos_set1"));
        assert!(s.contains("x0") && s.contains("x1"));
    }

    #[test]
    fn test_maximize_lp() {
        let mut m = make_milp();
        m.sense = OptDirection::Maximize;
        let w = LpWriter::new(cfg(OutputFormat::Lp));
        assert!(w.write_objective(&m).starts_with("Maximize\n"));
    }

    #[test]
    fn test_free_mps_format() {
        let c = EmissionConfig {
            format: OutputFormat::FreeMps,
            use_fixed_format_mps: false,
            ..cfg(OutputFormat::FreeMps)
        };
        let r = emit(&make_milp(), &c).unwrap();
        assert_eq!(r.format, OutputFormat::FreeMps);
        assert!(r.content.contains("NAME") && r.content.contains("ENDATA"));
    }

    #[test]
    fn test_default_names_fallback() {
        let mut m = make_milp();
        // Clear variable and constraint names to test fallback naming
        for var in &mut m.variables {
            var.name = String::new();
        }
        for con in &mut m.constraints {
            con.name = String::new();
        }
        let mw = MpsWriter::new(cfg(OutputFormat::Mps));
        let rows = mw.write_rows_section(&m);
        assert!(rows.contains("c0") && rows.contains("c1"));
        let lw = LpWriter::new(cfg(OutputFormat::Lp));
        let obj = lw.write_objective(&m);
        assert!(obj.contains("x0") && obj.contains("x1"));
    }

    #[test]
    fn test_cip_grb_not_implemented() {
        let m = make_milp();
        let c1 = EmissionConfig {
            format: OutputFormat::CipScip,
            ..cfg(OutputFormat::Mps)
        };
        assert!(emit(&m, &c1).is_err());
        let c2 = EmissionConfig {
            format: OutputFormat::GrbGurobi,
            ..cfg(OutputFormat::Mps)
        };
        assert!(emit(&m, &c2).is_err());
    }
}

