//! Solver backend emission: generate MPS and LP files from compiled MILP problems.
//!
//! Supports fixed-format MPS (80-column), free-format MPS, and CPLEX-style LP
//! file generation. Stubs for CIP (SCIP) and GRB (Gurobi) formats are provided.

use std::fmt::Write as FmtWrite;

use crate::pipeline::{IndicatorConstraint, MilpProblem, Sos1Set, VarType};
use crate::CompilerError;
use bicut_types::{ConstraintSense, OptDirection, VarBound};

/// Supported solver file formats.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
pub enum OutputFormat {
    Mps,
    FreeMps,
    Lp,
    CipScip,
    GrbGurobi,
}

impl std::fmt::Display for OutputFormat {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            Self::Mps => write!(f, "MPS"),
            Self::FreeMps => write!(f, "FreeMPS"),
            Self::Lp => write!(f, "LP"),
            Self::CipScip => write!(f, "CIP"),
            Self::GrbGurobi => write!(f, "GRB"),
        }
    }
}

/// Controls how a MILP is serialised to a solver file.
#[derive(Debug, Clone)]
pub struct EmissionConfig {
    pub format: OutputFormat,
    pub precision: usize,
    pub use_fixed_format_mps: bool,
    pub max_name_length: usize,
    pub include_comments: bool,
    pub compress_zeros: bool,
}

impl Default for EmissionConfig {
    fn default() -> Self {
        Self {
            format: OutputFormat::Mps,
            precision: 12,
            use_fixed_format_mps: true,
            max_name_length: 8,
            include_comments: true,
            compress_zeros: true,
        }
    }
}

/// The result of emitting a MILP to a solver file.
#[derive(Debug, Clone)]
pub struct EmissionResult {
    pub content: String,
    pub format: OutputFormat,
    pub num_vars_written: usize,
    pub num_constraints_written: usize,
    pub file_size_bytes: usize,
    pub warnings: Vec<String>,
}

// ── Helpers ────────────────────────────────────────────────────────

/// Extract all (row, coefficient) entries for column `col` from constraints.
pub fn get_column_entries(milp: &MilpProblem, col: usize) -> Vec<(usize, f64)> {
    milp.constraints
        .iter()
        .enumerate()
        .flat_map(|(row, con)| {
            con.coeffs
                .iter()
                .filter_map(move |&(c, v)| if c == col { Some((row, v)) } else { None })
        })
        .collect()
}

/// Default variable name when none is provided.
pub fn default_var_name(idx: usize) -> String {
    format!("x{}", idx)
}

/// Default constraint name when none is provided.
pub fn default_constraint_name(idx: usize) -> String {
    format!("c{}", idx)
}

fn resolve_var_name(milp: &MilpProblem, idx: usize) -> String {
    milp.variables
        .get(idx)
        .map(|v| &v.name)
        .filter(|s| !s.is_empty())
        .cloned()
        .unwrap_or_else(|| default_var_name(idx))
}

fn resolve_constraint_name(milp: &MilpProblem, idx: usize) -> String {
    milp.constraints
        .get(idx)
        .map(|c| &c.name)
        .filter(|s| !s.is_empty())
        .cloned()
        .unwrap_or_else(|| default_constraint_name(idx))
}

