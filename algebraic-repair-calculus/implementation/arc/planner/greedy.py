def greedy_plan(
    graph: Any,
    deltas: Dict[str, Any],
) -> GreedyRepairPlan:
    """Convenience: generate a greedy repair plan."""
    planner = GreedyRepairPlanner()
    return planner.plan(graph, deltas)


def greedy_plan_with_budget(
    graph: Any,
    deltas: Dict[str, Any],
    budget: float,
) -> GreedyRepairPlan:
    """Convenience: generate a greedy repair plan within budget."""
    planner = GreedyRepairPlanner()
    return planner.plan_with_budget(graph, deltas, budget)
