class GreedyHeuristic(Enum):
    """Heuristic for ordering repair candidates."""
    COST_FIRST = "cost_first"
    IMPACT_FIRST = "impact_first"
    COST_EFFECTIVENESS = "cost_effectiveness"
    TOPOLOGICAL = "topological"
    DEPTH_FIRST = "depth_first"
    BREADTH_FIRST = "breadth_first"


@dataclass
class RepairCandidate:
    """A candidate node for repair with cost-effectiveness scoring.

    Attributes
    ----------
    node_id : str
        The node to repair.
    repair_cost : float
        Estimated cost to repair this node.
    impact_score : float
        How much downstream impact this repair would address.
    cost_effectiveness : float
        Impact / cost ratio.
    downstream_count : int
        Number of downstream affected nodes covered by this repair.
    annihilated : bool
        Whether this node's delta is annihilated.
    depth : int
        Topological depth in the pipeline.
    """
    node_id: str = ""
    repair_cost: float = 0.0
    impact_score: float = 0.0
    cost_effectiveness: float = 0.0
    downstream_count: int = 0
    annihilated: bool = False
    depth: int = 0

    def __repr__(self) -> str:
        ann = " [annihilated]" if self.annihilated else ""
        return (
            f"RepairCandidate({self.node_id}, cost={self.repair_cost:.2f}, "
            f"impact={self.impact_score:.2f}, "
            f"eff={self.cost_effectiveness:.2f}{ann})"
        )


@dataclass
class GreedyRepairAction:
    """A repair action produced by the greedy planner.

    Attributes
    ----------
    node_id : str
        Node to repair.
    action_type : str
        Type of repair (RECOMPUTE, INCREMENTAL_UPDATE, SCHEMA_MIGRATE).
    estimated_cost : float
        Estimated cost.
    dependencies : list[str]
        Node ids that must be repaired first.
    priority : int
        Execution priority (lower = earlier).
    reason : str
        Why this action was selected.
    """
    node_id: str = ""
    action_type: str = "RECOMPUTE"
    estimated_cost: float = 0.0
    dependencies: List[str] = field(default_factory=list)
    priority: int = 0
    reason: str = ""


@dataclass
class GreedyRepairPlan:
    """A repair plan generated by the greedy planner.

    Attributes
    ----------
    actions : list[GreedyRepairAction]
        Ordered list of repair actions.
    total_cost : float
        Total estimated cost.
    nodes_repaired : set[str]
        Set of nodes that will be repaired.
    nodes_skipped : set[str]
        Set of affected nodes that were skipped (annihilated).
    planning_time_ms : float
        Time spent planning (milliseconds).
    heuristic_used : GreedyHeuristic
        The heuristic used for ordering.
    optimality_estimate : float
        Estimated ratio of this plan's cost to optimal (1.0 = optimal).
    """
    actions: List[GreedyRepairAction] = field(default_factory=list)
    total_cost: float = 0.0
    nodes_repaired: Set[str] = field(default_factory=set)
    nodes_skipped: Set[str] = field(default_factory=set)
    planning_time_ms: float = 0.0
    heuristic_used: GreedyHeuristic = GreedyHeuristic.COST_EFFECTIVENESS
    optimality_estimate: float = 0.5

    @property
    def action_count(self) -> int:
        return len(self.actions)

    def summary(self) -> str:
        lines = [
            f"GreedyRepairPlan:",
            f"  Actions:        {self.action_count}",
            f"  Total cost:     {self.total_cost:.2f}",
            f"  Nodes repaired: {len(self.nodes_repaired)}",
            f"  Nodes skipped:  {len(self.nodes_skipped)}",
            f"  Planning time:  {self.planning_time_ms:.2f} ms",
            f"  Heuristic:      {self.heuristic_used.value}",
            f"  Optimality est: {self.optimality_estimate:.2f}",
        ]
        return "\n".join(lines)


# =====================================================================
# Greedy Repair Planner
# =====================================================================


class GreedyRepairPlanner:
    """Fast greedy repair planner for real-time scenarios.

    Produces repair plans by greedily selecting repairs with the best
    cost-effectiveness ratio. Uses annihilation detection to skip
    unnecessary repairs.

    Parameters
    ----------
    cost_model : CostModel, optional
        Cost model for estimation.
    heuristic : GreedyHeuristic
        Heuristic for ordering repair candidates.
    enable_annihilation : bool
        Whether to check for annihilation.
    enable_post_processing : bool
        Whether to remove redundant actions after planning.
    max_actions : int
        Maximum number of repair actions to include.
    """

    def __init__(
        self,
        cost_model: Optional[CostModel] = None,
        heuristic: GreedyHeuristic = GreedyHeuristic.COST_EFFECTIVENESS,
        enable_annihilation: bool = True,
        enable_post_processing: bool = True,
        max_actions: int = 10_000,
    ) -> None:
        self._cost_model = cost_model or CostModel()
        self._heuristic = heuristic
        self._annihilation = enable_annihilation
        self._post_process = enable_post_processing
        self._max_actions = max_actions

    # ── Public API ────────────────────────────────────────────────

    def plan(
        self,
        graph: Any,
        deltas: Dict[str, Any],
        cost_model: Optional[CostModel] = None,
    ) -> GreedyRepairPlan:
        """Generate a greedy repair plan.

        Parameters
        ----------
        graph : PipelineGraph
            The pipeline DAG.
        deltas : dict[str, CompoundPerturbation]
            Mapping from node id to its delta.
        cost_model : CostModel, optional
            Override cost model.

        Returns
        -------
        GreedyRepairPlan
        """
        start_time = time.time()
        cm = cost_model or self._cost_model

        affected_nodes = self._compute_affected_nodes(graph, deltas)

        candidates = self._build_candidates(graph, affected_nodes, deltas, cm)

        if self._annihilation:
            candidates = self._filter_annihilated(candidates, deltas)

        sorted_candidates = self._sort_candidates(candidates)

        actions, covered = self._greedy_cover(
            graph, sorted_candidates, affected_nodes
        )

        if self._post_process:
            actions = self._remove_redundant(graph, actions, affected_nodes)

        self._assign_priorities(graph, actions)

        skipped = affected_nodes - {a.node_id for a in actions}
        total_cost = sum(a.estimated_cost for a in actions)
        planning_time = (time.time() - start_time) * 1000

        lower_bound = self._estimate_lower_bound(candidates)
        optimality = lower_bound / total_cost if total_cost > 0 else 1.0

        return GreedyRepairPlan(
            actions=actions,
            total_cost=total_cost,
            nodes_repaired={a.node_id for a in actions},
            nodes_skipped=skipped,
            planning_time_ms=planning_time,
            heuristic_used=self._heuristic,
            optimality_estimate=min(1.0, optimality),
        )

    def plan_incremental(
        self,
        graph: Any,
        previous_plan: GreedyRepairPlan,
        new_deltas: Dict[str, Any],
    ) -> GreedyRepairPlan:
        """Incrementally update a greedy repair plan.

        Reuses the previous plan where possible, only re-planning for
        newly affected nodes.

        Parameters
        ----------
        graph : PipelineGraph
            The pipeline DAG.
        previous_plan : GreedyRepairPlan
            The existing plan.
        new_deltas : dict[str, CompoundPerturbation]
            New deltas to incorporate.

        Returns
        -------
        GreedyRepairPlan
        """
        start_time = time.time()

        new_affected = self._compute_affected_nodes(graph, new_deltas)
        existing_covered = previous_plan.nodes_repaired

        additionally_affected = new_affected - existing_covered

        if not additionally_affected:
            planning_time = (time.time() - start_time) * 1000
            return GreedyRepairPlan(
                actions=list(previous_plan.actions),
                total_cost=previous_plan.total_cost,
                nodes_repaired=previous_plan.nodes_repaired,
                nodes_skipped=previous_plan.nodes_skipped,
                planning_time_ms=planning_time,
                heuristic_used=self._heuristic,
                optimality_estimate=previous_plan.optimality_estimate,
            )

        new_candidates = self._build_candidates(
            graph, additionally_affected, new_deltas, self._cost_model
        )
        sorted_new = self._sort_candidates(new_candidates)
        new_actions, _ = self._greedy_cover(
            graph, sorted_new, additionally_affected
        )

        all_actions = list(previous_plan.actions) + new_actions
        if self._post_process:
            all_affected = existing_covered | additionally_affected
            all_actions = self._remove_redundant(graph, all_actions, all_affected)

        self._assign_priorities(graph, all_actions)

        total_cost = sum(a.estimated_cost for a in all_actions)
        all_repaired = {a.node_id for a in all_actions}
        all_skipped = (existing_covered | new_affected) - all_repaired
        planning_time = (time.time() - start_time) * 1000

        return GreedyRepairPlan(
            actions=all_actions,
            total_cost=total_cost,
            nodes_repaired=all_repaired,
            nodes_skipped=all_skipped,
            planning_time_ms=planning_time,
            heuristic_used=self._heuristic,
        )

    def plan_with_budget(
        self,
        graph: Any,
        deltas: Dict[str, Any],
        cost_budget: float,
    ) -> GreedyRepairPlan:
        """Generate a repair plan within a cost budget.

        Greedily selects the most cost-effective repairs until the
        budget is exhausted.

        Parameters
        ----------
        graph : PipelineGraph
            The pipeline DAG.
        deltas : dict[str, CompoundPerturbation]
            Node deltas.
        cost_budget : float
            Maximum total repair cost.

        Returns
        -------
        GreedyRepairPlan
        """
        start_time = time.time()
        affected_nodes = self._compute_affected_nodes(graph, deltas)
        candidates = self._build_candidates(
            graph, affected_nodes, deltas, self._cost_model
        )

        if self._annihilation:
            candidates = self._filter_annihilated(candidates, deltas)

        sorted_candidates = self._sort_candidates(candidates)

        actions: List[GreedyRepairAction] = []
        current_cost = 0.0
        covered: Set[str] = set()

        for candidate in sorted_candidates:
            if current_cost + candidate.repair_cost > cost_budget:
                continue
            if candidate.node_id in covered:
                continue

            action = self._candidate_to_action(candidate)
            actions.append(action)
            current_cost += candidate.repair_cost
            covered.add(candidate.node_id)

            downstream = self._get_downstream_affected(
                graph, candidate.node_id, affected_nodes
            )
            covered.update(downstream)

        self._assign_priorities(graph, actions)
        planning_time = (time.time() - start_time) * 1000

        return GreedyRepairPlan(
            actions=actions,
            total_cost=current_cost,
            nodes_repaired={a.node_id for a in actions},
            nodes_skipped=affected_nodes - covered,
            planning_time_ms=planning_time,
            heuristic_used=self._heuristic,
        )

    # ── Internal: Affected Node Computation ───────────────────────

    def _compute_affected_nodes(
        self,
        graph: Any,
        deltas: Dict[str, Any],
    ) -> Set[str]:
        """Compute all affected nodes from source deltas."""
        affected: Set[str] = set()

        for source_id in deltas:
            if not graph.has_node(source_id):
                continue
            affected.add(source_id)
            affected.update(graph.descendants(source_id))

        return affected

    # ── Internal: Candidate Building ──────────────────────────────

    def _build_candidates(
        self,
        graph: Any,
        affected: Set[str],
        deltas: Dict[str, Any],
        cm: CostModel,
    ) -> List[RepairCandidate]:
        """Build repair candidates for all affected nodes."""
        candidates: List[RepairCandidate] = []
        topo = graph.topological_sort()
        depth_map = self._compute_depth_map(graph, topo)

        for nid in affected:
            node = graph.get_node(nid)

            repair_cost = cm.estimate_recompute_cost(node).total_weighted_cost
            if repair_cost <= 0:
                repair_cost = 1.0

            downstream = self._get_downstream_affected(graph, nid, affected)
            downstream_count = len(downstream)

            impact = downstream_count + 1.0

            if downstream_count > 0:
                downstream_cost = sum(
                    cm.estimate_recompute_cost(graph.get_node(d)).total_weighted_cost
                    for d in downstream if graph.has_node(d)
                )
                impact += downstream_cost

            effectiveness = impact / repair_cost if repair_cost > 0 else impact

            candidates.append(RepairCandidate(
                node_id=nid,
                repair_cost=repair_cost,
                impact_score=impact,
                cost_effectiveness=effectiveness,
                downstream_count=downstream_count,
                annihilated=False,
                depth=depth_map.get(nid, 0),
            ))

        return candidates

    def _filter_annihilated(
        self,
        candidates: List[RepairCandidate],
        deltas: Dict[str, Any],
    ) -> List[RepairCandidate]:
        """Filter out candidates whose delta is annihilated."""
        filtered: List[RepairCandidate] = []

        for candidate in candidates:
            delta = deltas.get(candidate.node_id)
            if delta is not None and self._is_delta_annihilated(delta):
                candidate.annihilated = True
            filtered.append(candidate)

        return filtered

    def _sort_candidates(
        self,
        candidates: List[RepairCandidate],
    ) -> List[RepairCandidate]:
        """Sort candidates by the configured heuristic."""
        if self._heuristic == GreedyHeuristic.COST_FIRST:
            return sorted(candidates, key=lambda c: c.repair_cost)

        if self._heuristic == GreedyHeuristic.IMPACT_FIRST:
            return sorted(candidates, key=lambda c: -c.impact_score)

        if self._heuristic == GreedyHeuristic.COST_EFFECTIVENESS:
            return sorted(candidates, key=lambda c: -c.cost_effectiveness)

        if self._heuristic == GreedyHeuristic.TOPOLOGICAL:
            return sorted(candidates, key=lambda c: c.depth)

        if self._heuristic == GreedyHeuristic.DEPTH_FIRST:
            return sorted(candidates, key=lambda c: -c.depth)

        if self._heuristic == GreedyHeuristic.BREADTH_FIRST:
            return sorted(candidates, key=lambda c: c.depth)

        return sorted(candidates, key=lambda c: -c.cost_effectiveness)

    # ── Internal: Greedy Cover ────────────────────────────────────

    def _greedy_cover(
        self,
        graph: Any,
        sorted_candidates: List[RepairCandidate],
        affected: Set[str],
    ) -> Tuple[List[GreedyRepairAction], Set[str]]:
        """Greedily select repairs to cover all affected nodes."""
        actions: List[GreedyRepairAction] = []
        covered: Set[str] = set()
        action_count = 0

        for candidate in sorted_candidates:
            if action_count >= self._max_actions:
                break

            if candidate.node_id in covered:
                continue

            if candidate.annihilated:
                covered.add(candidate.node_id)
                continue

            action = self._candidate_to_action(candidate)
            actions.append(action)
            covered.add(candidate.node_id)
            action_count += 1

            downstream = self._get_downstream_affected(
                graph, candidate.node_id, affected
            )
            covered.update(downstream)

        return actions, covered

    def _candidate_to_action(
        self,
        candidate: RepairCandidate,
    ) -> GreedyRepairAction:
        """Convert a repair candidate to a repair action."""
        if candidate.repair_cost < 1.0:
            action_type = "INCREMENTAL_UPDATE"
        else:
            action_type = "RECOMPUTE"

        return GreedyRepairAction(
            node_id=candidate.node_id,
            action_type=action_type,
            estimated_cost=candidate.repair_cost,
            reason=(
                f"Greedy selection: effectiveness={candidate.cost_effectiveness:.2f}, "
                f"covers {candidate.downstream_count} downstream nodes"
            ),
        )

    # ── Internal: Post-Processing ─────────────────────────────────

    def _remove_redundant(
        self,
        graph: Any,
        actions: List[GreedyRepairAction],
        affected: Set[str],
    ) -> List[GreedyRepairAction]:
        """Remove redundant actions from the plan.

        An action is redundant if removing it still covers all affected nodes
        through other actions' downstream coverage.
        """
        essential: List[GreedyRepairAction] = []
        covered_without: Set[str] = set()

        for i, action in enumerate(actions):
            other_coverage: Set[str] = set()
            for j, other in enumerate(actions):
                if i == j:
                    continue
                other_coverage.add(other.node_id)
                other_coverage.update(
                    self._get_downstream_affected(
                        graph, other.node_id, affected
                    )
                )

            uncovered = affected - other_coverage
            if uncovered:
                essential.append(action)

        return essential if essential else actions

    def _assign_priorities(
        self,
        graph: Any,
        actions: List[GreedyRepairAction],
    ) -> None:
        """Assign execution priorities to actions based on dependencies."""
        action_nodes = {a.node_id for a in actions}
        topo = graph.topological_sort()
        topo_index = {nid: i for i, nid in enumerate(topo)}

        for action in actions:
            action.priority = topo_index.get(action.node_id, 0)

            deps = []
            preds = graph.predecessors(action.node_id)
            for pred in preds:
                if pred in action_nodes:
                    deps.append(pred)
            action.dependencies = deps

        actions.sort(key=lambda a: a.priority)

    # ── Internal: Helpers ─────────────────────────────────────────

    def _get_downstream_affected(
        self,
        graph: Any,
        node_id: str,
        affected: Set[str],
    ) -> Set[str]:
        """Get downstream nodes of node_id that are in the affected set."""
        try:
            descendants = graph.descendants(node_id)
            return descendants & affected
        except Exception:
            return set()

    @staticmethod
    def _compute_depth_map(graph: Any, topo: List[str]) -> Dict[str, int]:
        """Compute topological depth for each node."""
        depth: Dict[str, int] = {}

        for nid in topo:
            preds = graph.predecessors(nid)
            if not preds:
                depth[nid] = 0
            else:
                depth[nid] = max(depth.get(p, 0) for p in preds) + 1

        return depth

    @staticmethod
    def _is_delta_annihilated(delta: Any) -> bool:
        """Check if a delta has been annihilated (all empty)."""
        s_empty = (
            not hasattr(delta, "schema_delta")
            or delta.schema_delta is None
            or len(delta.schema_delta.operations) == 0
        )
        d_empty = (
            not hasattr(delta, "data_delta")
            or delta.data_delta is None
            or len(delta.data_delta.operations) == 0
        )
        q_empty = (
            not hasattr(delta, "quality_delta")
            or delta.quality_delta is None
            or len(delta.quality_delta.operations) == 0
        )
        return s_empty and d_empty and q_empty

    @staticmethod
    def _estimate_lower_bound(
        candidates: List[RepairCandidate],
    ) -> float:
        """Estimate a lower bound on the optimal repair cost.

        Uses the sum of per-node minimum costs as a relaxation.
        """
        if not candidates:
            return 0.0

        return sum(c.repair_cost for c in candidates if not c.annihilated) * 0.5


