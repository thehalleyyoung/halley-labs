"""
Greedy Repair Planner
======================

Fast greedy repair planner for real-time scenarios. Produces repair plans
by greedily selecting the cheapest repairs that cover all affected nodes.
May produce suboptimal plans compared to DP/LP, but runs in O(n log n).

Algorithm:
  1. Identify all affected nodes downstream of the perturbation.
  2. Sort affected nodes by cost-effectiveness (impact / cost).
  3. Greedily select cheapest repairs that cover all affected nodes.
  4. Use annihilation detection to skip unnecessary repairs.
  5. Post-process to remove redundant actions.
"""

