/// Generate a trace for a linear recurrence x' = a*x + b.
pub fn linear_recurrence_trace(
    init: u64,
    a: u64,
    b: u64,
    num_steps: usize,
) -> AIRTrace {
    let mut layout = TraceLayout::new();
    layout.add_column("x", ColumnType::State);

    let a_f = GoldilocksField::new(a);
    let b_f = GoldilocksField::new(b);
    let mut trace = AIRTrace::new(num_steps, layout);
    let mut x = GoldilocksField::new(init);

    for r in 0..num_steps {
        trace.set(r, 0, x);
        x = a_f * x + b_f;
    }
    trace
}

/// Generate a trace that computes x^n via repeated squaring.
pub fn exponentiation_trace(
    base: u64,
    exponent: u64,
    num_steps: usize,
) -> AIRTrace {
    let mut layout = TraceLayout::new();
    layout.add_column("base", ColumnType::Input);
    layout.add_column("exp_remaining", ColumnType::State);
    layout.add_column("accumulator", ColumnType::State);
    layout.add_column("current_power", ColumnType::State);

    let b = GoldilocksField::new(base);
    let mut trace = AIRTrace::new(num_steps, layout);

    let mut exp_rem = exponent;
    let mut acc = GoldilocksField::ONE;
    let mut cur_pow = b;

    for r in 0..num_steps {
        trace.set(r, 0, b);
        trace.set(r, 1, GoldilocksField::new(exp_rem));
        trace.set(r, 2, acc);
        trace.set(r, 3, cur_pow);

        if exp_rem & 1 == 1 {
            acc = acc * cur_pow;
        }
        cur_pow = cur_pow * cur_pow;
        exp_rem >>= 1;
    }
    trace
}

