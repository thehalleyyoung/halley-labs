// AIR (Algebraic Intermediate Representation) module for the Spectacles STARK prover.
//
// This module defines the constraint system used to express computation as
// polynomial identity checks over execution traces.  The core abstractions are:
//
//   SymbolicExpression – an AST for arithmetic-circuit constraints
//   AIRConstraint      – a named, typed wrapper around a symbolic expression
//   TraceLayout        – schema describing the columns of an execution trace
//   AIRTrace           – the concrete 2-D field-element trace table
//   AIRProgram         – a complete set of constraints + layout
//   ConstraintComposer – random-linear-combination composer
//   PeriodicColumn     – repeating-pattern helper
//   BoundaryDescriptor – (column, row, value) boundary specification
//   ConstraintCompiler – symbolic-expression optimizer & compiler

use std::collections::{HashMap, HashSet, BTreeSet};
use std::fmt;
use std::ops::{Add, Mul, Sub, Neg};

use super::goldilocks::GoldilocksField;

// ═══════════════════════════════════════════════════════════════════════════
// ConstraintType
// ═══════════════════════════════════════════════════════════════════════════

/// Classifies AIR constraints by the rows they apply to.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
pub enum ConstraintType {
    /// Must hold at a single specified row (typically first or last).
    Boundary,
    /// Must hold at every consecutive pair (row i, row i+1).
    Transition,
    /// Must hold at rows whose index is divisible by a given period.
    Periodic,
    /// A derived constraint produced by composing several sub-constraints
    /// with random challenges (used internally by the prover).
    Composition,
}

impl fmt::Display for ConstraintType {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        match self {
            ConstraintType::Boundary    => write!(f, "Boundary"),
            ConstraintType::Transition  => write!(f, "Transition"),
            ConstraintType::Periodic    => write!(f, "Periodic"),
            ConstraintType::Composition => write!(f, "Composition"),
        }
    }
}

