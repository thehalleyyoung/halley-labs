#[derive(Debug, Clone)]
pub struct ExpressionStats {
    pub node_count: usize,
    pub degree: usize,
    pub num_variables: usize,
    pub uses_next_row: bool,
    pub is_constant: bool,
    pub max_column: usize,
}

