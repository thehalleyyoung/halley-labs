    #[test]
    fn test_compiled_expression_complex() {
        // (cur[0] * cur[1]) + nxt[0]
        let expr = SymbolicExpression::cur(0) * SymbolicExpression::cur(1)
            + SymbolicExpression::nxt(0);
        let compiled = ExpressionEvaluator::compile(&expr);
        let current = vec![GoldilocksField::new(3), GoldilocksField::new(4)];
        let next = vec![GoldilocksField::new(5)];
        // 3*4 + 5 = 17
        assert_eq!(compiled.evaluate(&current, &next), GoldilocksField::new(17));
    }

