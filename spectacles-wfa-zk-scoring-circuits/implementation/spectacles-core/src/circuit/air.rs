/// Builder pattern for constructing AIR programs step by step.
pub struct AIRBuilder {
    name: String,
    layout: TraceLayout,
    constraints: Vec<AIRConstraint>,
    boundary_descriptors: Vec<BoundaryDescriptor>,
    periodic_columns: Vec<PeriodicColumn>,
    next_col: usize,
}

impl AIRBuilder {
    /// Create a new builder with the given program name.
    pub fn new(name: &str) -> Self {
        Self {
            name: name.to_string(),
            layout: TraceLayout::new(),
            constraints: Vec::new(),
            boundary_descriptors: Vec::new(),
            periodic_columns: Vec::new(),
            next_col: 0,
        }
    }

    /// Add `count` state columns to the layout.
    pub fn add_state_columns(&mut self, count: usize) -> &mut Self {
        for i in 0..count {
            self.layout.add_column(
                format!("state_{}", self.next_col + i),
                ColumnType::State,
            );
        }
        self.next_col += count;
        self
    }

    /// Add `count` input columns to the layout.
    pub fn add_input_columns(&mut self, count: usize) -> &mut Self {
        for i in 0..count {
            self.layout.add_column(
                format!("input_{}", self.next_col + i),
                ColumnType::Input,
            );
        }
        self.next_col += count;
        self
    }

    /// Add `count` auxiliary columns to the layout.
    pub fn add_aux_columns(&mut self, count: usize) -> &mut Self {
        for i in 0..count {
            self.layout.add_column(
                format!("aux_{}", self.next_col + i),
                ColumnType::Auxiliary,
            );
        }
        self.next_col += count;
        self
    }

    /// Add a boundary constraint: trace[row][col] = value.
    pub fn boundary_at(
        &mut self,
        row: usize,
        col: usize,
        value: GoldilocksField,
    ) -> &mut Self {
        let desc = BoundaryDescriptor::new(col, row, value);
        self.constraints.push(desc.to_constraint());
        self.boundary_descriptors.push(desc);
        self
    }

    /// Add a transition constraint (must hold at every consecutive row pair).
    pub fn transition(
        &mut self,
        name: &str,
        expr: SymbolicExpression,
    ) -> &mut Self {
        self.constraints.push(AIRConstraint::new(
            name,
            ConstraintType::Transition,
            expr,
            None,
        ));
        self
    }

    /// Add a periodic constraint with the given period.
    pub fn periodic(
        &mut self,
        name: &str,
        expr: SymbolicExpression,
        period: usize,
    ) -> &mut Self {
        self.constraints.push(AIRConstraint::new_periodic(name, expr, period));
        self
    }

    /// Consume the builder and produce the final AIRProgram.
    pub fn build(self) -> AIRProgram {
        let mut program = AIRProgram::new(self.name, self.layout);
        for c in self.constraints {
            program.add_constraint(c);
        }
        for bd in self.boundary_descriptors {
            program.boundary_descriptors.push(bd);
        }
        for pc in self.periodic_columns {
            program.add_periodic_column(pc);
        }
        program
    }
}

