/// A single stack-machine instruction for compiled expression evaluation.
#[derive(Debug, Clone)]
pub enum Instruction {
    /// Push a constant onto the stack.
    LoadConst(GoldilocksField),
    /// Push current_row[col] onto the stack.
    LoadCurrent(usize),
    /// Push next_row[col] onto the stack.
    LoadNext(usize),
    /// Pop two, push their sum.
    Add,
    /// Pop two, push their product.
    Mul,
    /// Pop two, push (second - top).
    Sub,
    /// Pop one, push its negation.
    Neg,
}

/// A compiled expression stored as a sequence of stack-machine instructions.
#[derive(Debug, Clone)]
pub struct CompiledExpression {
    pub instructions: Vec<Instruction>,
}

impl CompiledExpression {
    /// Execute the compiled expression on concrete row data.
    pub fn evaluate(
        &self,
        current: &[GoldilocksField],
        next: &[GoldilocksField],
    ) -> GoldilocksField {
        let mut stack: Vec<GoldilocksField> = Vec::with_capacity(self.instructions.len());
        for instr in &self.instructions {
            match instr {
                Instruction::LoadConst(c) => stack.push(*c),
                Instruction::LoadCurrent(col) => stack.push(current[*col]),
                Instruction::LoadNext(col) => stack.push(next[*col]),
                Instruction::Add => {
                    let b = stack.pop().unwrap();
                    let a = stack.pop().unwrap();
                    stack.push(a + b);
                }
                Instruction::Mul => {
                    let b = stack.pop().unwrap();
                    let a = stack.pop().unwrap();
                    stack.push(a * b);
                }
                Instruction::Sub => {
                    let b = stack.pop().unwrap();
                    let a = stack.pop().unwrap();
                    stack.push(a - b);
                }
                Instruction::Neg => {
                    let a = stack.pop().unwrap();
                    stack.push(-a);
                }
            }
        }
        stack.pop().unwrap_or(GoldilocksField::ZERO)
    }
}

