/// Compiles and optimises symbolic expressions for efficient evaluation.
///
/// Optimisations include:
///   - Constant folding
///   - Common sub-expression elimination (CSE)
///   - Dead-code removal
///   - Algebraic identity simplification
#[derive(Debug)]
pub struct ConstraintCompiler {
    /// Cache of previously compiled sub-expressions (hash → simplified form).
    cse_cache: HashMap<u64, SymbolicExpression>,
    /// Statistics: number of CSE hits.
    cse_hits: usize,
    /// Statistics: number of constant folds.
    constant_folds: usize,
    /// Maximum recursion depth for optimization.
    max_depth: usize,
}

impl ConstraintCompiler {
    /// Create a new compiler with default settings.
    pub fn new() -> Self {
        Self {
            cse_cache: HashMap::new(),
            cse_hits: 0,
            constant_folds: 0,
            max_depth: 64,
        }
    }

    /// Create a compiler with custom max recursion depth.
    pub fn with_max_depth(max_depth: usize) -> Self {
        Self {
            cse_cache: HashMap::new(),
            cse_hits: 0,
            constant_folds: 0,
            max_depth,
        }
    }

    /// Compile an expression: normalize, simplify, and apply CSE.
    pub fn compile(&mut self, expr: &SymbolicExpression) -> SymbolicExpression {
        let normalized = expr.normalize();
        let simplified = normalized.simplify_full();
        self.apply_cse(&simplified, 0)
    }

    /// Apply common sub-expression elimination.
    fn apply_cse(&mut self, expr: &SymbolicExpression, depth: usize) -> SymbolicExpression {
        if depth > self.max_depth {
            return expr.clone();
        }

        let hash = self.hash_expression(expr);
        if let Some(cached) = self.cse_cache.get(&hash) {
            self.cse_hits += 1;
            return cached.clone();
        }

        let result = match expr {
            SymbolicExpression::Constant(_)
            | SymbolicExpression::Variable { .. }
            | SymbolicExpression::CurrentRow(_)
            | SymbolicExpression::NextRow(_) => expr.clone(),

            SymbolicExpression::Add(a, b) => {
                let ca = self.apply_cse(a, depth + 1);
                let cb = self.apply_cse(b, depth + 1);
                if let (Some(va), Some(vb)) = (ca.constant_value(), cb.constant_value()) {
                    self.constant_folds += 1;
                    SymbolicExpression::Constant(va + vb)
                } else {
                    SymbolicExpression::Add(Box::new(ca), Box::new(cb))
                }
            }

            SymbolicExpression::Sub(a, b) => {
                let ca = self.apply_cse(a, depth + 1);
                let cb = self.apply_cse(b, depth + 1);
                if let (Some(va), Some(vb)) = (ca.constant_value(), cb.constant_value()) {
                    self.constant_folds += 1;
                    SymbolicExpression::Constant(va - vb)
                } else {
                    SymbolicExpression::Sub(Box::new(ca), Box::new(cb))
                }
            }

            SymbolicExpression::Mul(a, b) => {
                let ca = self.apply_cse(a, depth + 1);
                let cb = self.apply_cse(b, depth + 1);
                if let (Some(va), Some(vb)) = (ca.constant_value(), cb.constant_value()) {
                    self.constant_folds += 1;
                    SymbolicExpression::Constant(va * vb)
                } else {
                    SymbolicExpression::Mul(Box::new(ca), Box::new(cb))
                }
            }

            SymbolicExpression::Neg(a) => {
                let ca = self.apply_cse(a, depth + 1);
                if let Some(v) = ca.constant_value() {
                    self.constant_folds += 1;
                    SymbolicExpression::Constant(-v)
                } else {
                    SymbolicExpression::Neg(Box::new(ca))
                }
            }

            SymbolicExpression::Pow(base, exp) => {
                let cb = self.apply_cse(base, depth + 1);
                if let Some(v) = cb.constant_value() {
                    self.constant_folds += 1;
                    SymbolicExpression::Constant(v.pow(*exp))
                } else {
                    SymbolicExpression::Pow(Box::new(cb), *exp)
                }
            }
        };

        self.cse_cache.insert(hash, result.clone());
        result
    }

    /// A simple FNV-1a-style hash for expression identity.
    fn hash_expression(&self, expr: &SymbolicExpression) -> u64 {
        let mut h: u64 = 14695981039346656037;
        self.hash_expr_inner(expr, &mut h);
        h
    }

    fn hash_expr_inner(&self, expr: &SymbolicExpression, h: &mut u64) {
        const FNV_PRIME: u64 = 1099511628211;
        match expr {
            SymbolicExpression::Constant(c) => {
                *h ^= 1;
                *h = h.wrapping_mul(FNV_PRIME);
                *h ^= c.to_canonical();
                *h = h.wrapping_mul(FNV_PRIME);
            }
            SymbolicExpression::Variable { col, row_offset } => {
                *h ^= 2;
                *h = h.wrapping_mul(FNV_PRIME);
                *h ^= *col as u64;
                *h = h.wrapping_mul(FNV_PRIME);
                *h ^= *row_offset as u64;
                *h = h.wrapping_mul(FNV_PRIME);
            }
            SymbolicExpression::CurrentRow(col) => {
                *h ^= 2;
                *h = h.wrapping_mul(FNV_PRIME);
                *h ^= *col as u64;
                *h = h.wrapping_mul(FNV_PRIME);
                *h ^= 0u64;
                *h = h.wrapping_mul(FNV_PRIME);
            }
            SymbolicExpression::NextRow(col) => {
                *h ^= 2;
                *h = h.wrapping_mul(FNV_PRIME);
                *h ^= *col as u64;
                *h = h.wrapping_mul(FNV_PRIME);
                *h ^= 1u64;
                *h = h.wrapping_mul(FNV_PRIME);
            }
            SymbolicExpression::Add(a, b) => {
                *h ^= 3;
                *h = h.wrapping_mul(FNV_PRIME);
                self.hash_expr_inner(a, h);
                self.hash_expr_inner(b, h);
            }
            SymbolicExpression::Mul(a, b) => {
                *h ^= 4;
                *h = h.wrapping_mul(FNV_PRIME);
                self.hash_expr_inner(a, h);
                self.hash_expr_inner(b, h);
            }
            SymbolicExpression::Sub(a, b) => {
                *h ^= 5;
                *h = h.wrapping_mul(FNV_PRIME);
                self.hash_expr_inner(a, h);
                self.hash_expr_inner(b, h);
            }
            SymbolicExpression::Neg(a) => {
                *h ^= 6;
                *h = h.wrapping_mul(FNV_PRIME);
                self.hash_expr_inner(a, h);
            }
            SymbolicExpression::Pow(base, exp) => {
                *h ^= 7;
                *h = h.wrapping_mul(FNV_PRIME);
                self.hash_expr_inner(base, h);
                *h ^= *exp;
                *h = h.wrapping_mul(FNV_PRIME);
            }
        }
    }

    /// Apply the full optimization pipeline: normalize → constant fold →
    /// simplify → CSE → simplify again.
    pub fn optimize(&mut self, expr: &SymbolicExpression) -> SymbolicExpression {
        let normalized = expr.normalize();
        let folded = self.constant_fold(&normalized, 0);
        let simplified = folded.simplify_full();
        let cse_applied = self.apply_cse(&simplified, 0);
        cse_applied.simplify_full()
    }

    /// Pure constant folding pass.
    fn constant_fold(&mut self, expr: &SymbolicExpression, depth: usize) -> SymbolicExpression {
        if depth > self.max_depth {
            return expr.clone();
        }
        match expr {
            SymbolicExpression::Constant(_) | SymbolicExpression::Variable { .. }
            | SymbolicExpression::CurrentRow(_) | SymbolicExpression::NextRow(_) => {
                expr.clone()
            }

            SymbolicExpression::Add(a, b) => {
                let fa = self.constant_fold(a, depth + 1);
                let fb = self.constant_fold(b, depth + 1);
                match (fa.constant_value(), fb.constant_value()) {
                    (Some(va), Some(vb)) => {
                        self.constant_folds += 1;
                        SymbolicExpression::Constant(va + vb)
                    }
                    _ => SymbolicExpression::Add(Box::new(fa), Box::new(fb)),
                }
            }
            SymbolicExpression::Sub(a, b) => {
                let fa = self.constant_fold(a, depth + 1);
                let fb = self.constant_fold(b, depth + 1);
                match (fa.constant_value(), fb.constant_value()) {
                    (Some(va), Some(vb)) => {
                        self.constant_folds += 1;
                        SymbolicExpression::Constant(va - vb)
                    }
                    _ => SymbolicExpression::Sub(Box::new(fa), Box::new(fb)),
                }
            }
            SymbolicExpression::Mul(a, b) => {
                let fa = self.constant_fold(a, depth + 1);
                let fb = self.constant_fold(b, depth + 1);
                match (fa.constant_value(), fb.constant_value()) {
                    (Some(va), Some(vb)) => {
                        self.constant_folds += 1;
                        SymbolicExpression::Constant(va * vb)
                    }
                    _ => SymbolicExpression::Mul(Box::new(fa), Box::new(fb)),
                }
            }
            SymbolicExpression::Neg(a) => {
                let fa = self.constant_fold(a, depth + 1);
                if let Some(v) = fa.constant_value() {
                    self.constant_folds += 1;
                    SymbolicExpression::Constant(-v)
                } else {
                    SymbolicExpression::Neg(Box::new(fa))
                }
            }
            SymbolicExpression::Pow(base, exp) => {
                let fb = self.constant_fold(base, depth + 1);
                if let Some(v) = fb.constant_value() {
                    self.constant_folds += 1;
                    SymbolicExpression::Constant(v.pow(*exp))
                } else {
                    SymbolicExpression::Pow(Box::new(fb), *exp)
                }
            }
        }
    }

    /// Estimate the algebraic degree of an expression.
    pub fn estimate_degree(&self, expr: &SymbolicExpression) -> usize {
        expr.degree()
    }

    /// Return the number of CSE cache hits.
    pub fn cse_hit_count(&self) -> usize {
        self.cse_hits
    }

    /// Return the number of constant folds performed.
    pub fn constant_fold_count(&self) -> usize {
        self.constant_folds
    }

    /// Compile a list of constraints and return optimized versions.
    pub fn compile_all(
        &mut self,
        constraints: &[AIRConstraint],
    ) -> Vec<AIRConstraint> {
        constraints
            .iter()
            .map(|c| {
                let optimized = self.compile(&c.expression);
                let degree = optimized.degree();
                AIRConstraint {
                    name: c.name.clone(),
                    constraint_type: c.constraint_type,
                    expression: optimized,
                    boundary_row: c.boundary_row,
                    degree,
                    period: c.period,
                }
            })
            .collect()
    }

    /// Analyse an expression and report statistics.
    pub fn analyse(&self, expr: &SymbolicExpression) -> ExpressionStats {
        let vars = expr.variables_used();
        ExpressionStats {
            node_count: expr.node_count(),
            degree: expr.degree(),
            num_variables: vars.len(),
            uses_next_row: expr.uses_next_row(),
            is_constant: expr.is_constant(),
            max_column: vars.iter().map(|(c, _)| *c).max().unwrap_or(0),
        }
    }

    /// Reset internal caches and statistics.
    pub fn reset(&mut self) {
        self.cse_cache.clear();
        self.cse_hits = 0;
        self.constant_folds = 0;
    }
}

impl Default for ConstraintCompiler {
    fn default() -> Self {
        Self::new()
    }
}

