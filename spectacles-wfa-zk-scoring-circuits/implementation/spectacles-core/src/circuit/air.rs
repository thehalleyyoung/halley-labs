// ExpressionEvaluator
// ═══════════════════════════════════════════════════════════════════════════

/// Optimized expression evaluator that compiles symbolic expressions into
/// stack-machine instructions for faster repeated evaluation.
pub struct ExpressionEvaluator;

impl ExpressionEvaluator {
    /// Compile a symbolic expression into a sequence of stack instructions.
    pub fn compile(expr: &SymbolicExpression) -> CompiledExpression {
        let mut instructions = Vec::new();
        Self::compile_recursive(expr, &mut instructions);
        CompiledExpression { instructions }
    }

    fn compile_recursive(expr: &SymbolicExpression, out: &mut Vec<Instruction>) {
        match expr {
            SymbolicExpression::Constant(c) => {
                out.push(Instruction::LoadConst(*c));
            }
            SymbolicExpression::Variable { col, row_offset } => {
                if *row_offset <= 0 {
                    out.push(Instruction::LoadCurrent(*col));
                } else {
                    out.push(Instruction::LoadNext(*col));
                }
            }
            SymbolicExpression::CurrentRow(col) => {
                out.push(Instruction::LoadCurrent(*col));
            }
            SymbolicExpression::NextRow(col) => {
                out.push(Instruction::LoadNext(*col));
            }
            SymbolicExpression::Add(a, b) => {
                Self::compile_recursive(a, out);
                Self::compile_recursive(b, out);
                out.push(Instruction::Add);
            }
            SymbolicExpression::Mul(a, b) => {
                Self::compile_recursive(a, out);
                Self::compile_recursive(b, out);
                out.push(Instruction::Mul);
            }
            SymbolicExpression::Sub(a, b) => {
                Self::compile_recursive(a, out);
                Self::compile_recursive(b, out);
                out.push(Instruction::Sub);
            }
            SymbolicExpression::Neg(a) => {
                Self::compile_recursive(a, out);
                out.push(Instruction::Neg);
            }
            SymbolicExpression::Pow(base, exp) => {
                // Compile as repeated squaring
                if *exp == 0 {
                    out.push(Instruction::LoadConst(GoldilocksField::ONE));
                } else {
                    Self::compile_recursive(base, out);
                    for _ in 1..*exp {
                        // Duplicate top of stack by re-compiling base, then multiply
                        Self::compile_recursive(base, out);
                        out.push(Instruction::Mul);
                    }
                }
            }
        }
    }

    /// Evaluate a pre-compiled expression on concrete row values.
    pub fn evaluate_compiled(
        compiled: &CompiledExpression,
        current: &[GoldilocksField],
        next: &[GoldilocksField],
    ) -> GoldilocksField {
        compiled.evaluate(current, next)
    }

    /// Batch-evaluate a compiled expression over all consecutive row pairs in a trace.
    /// Returns one value per row (the last row uses itself as `next`).
    pub fn batch_evaluate(
        compiled: &CompiledExpression,
        trace: &AIRTrace,
    ) -> Vec<GoldilocksField> {
        let n = trace.num_rows;
        let mut results = Vec::with_capacity(n);
        for r in 0..n {
            let current = trace.get_row(r);
            let next = if r + 1 < n {
                trace.get_row(r + 1)
            } else {
                trace.get_row(r)
            };
            results.push(compiled.evaluate(current, next));
        }
        results
    }
}

