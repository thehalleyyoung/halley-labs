// AIRAnalyzer
// ═══════════════════════════════════════════════════════════════════════════

/// Static analyzer for AIR program properties.
pub struct AIRAnalyzer;

impl AIRAnalyzer {
    /// Run a full analysis on the given AIR program.
    pub fn analyze(program: &AIRProgram) -> AIRAnalysis {
        let num_boundary = program.boundary_constraints().len();
        let num_transition = program.transition_constraints().len();
        let max_degree = Self::max_transition_degree(program)
            .max(Self::max_boundary_degree(program));
        let active = Self::active_columns(program);
        let deps = Self::column_dependencies(program);
        let cost = Self::estimated_verification_cost(program);

        AIRAnalysis {
            num_constraints: program.constraints.len(),
            num_boundary,
            num_transition,
            max_degree,
            active_columns: active,
            column_deps: deps,
            estimated_cost: cost,
        }
    }

    /// Build a constraint graph from the program.
    pub fn constraint_graph(program: &AIRProgram) -> ConstraintGraph {
        let n = program.constraints.len();
        let node_columns: Vec<BTreeSet<usize>> = program.constraints.iter()
            .map(|c| c.referenced_columns())
            .collect();

        let mut adjacency: Vec<HashSet<usize>> = vec![HashSet::new(); n];
        for i in 0..n {
            for j in (i + 1)..n {
                let shared = node_columns[i].intersection(&node_columns[j]).next().is_some();
                if shared {
                    adjacency[i].insert(j);
                    adjacency[j].insert(i);
                }
            }
        }

        ConstraintGraph {
            num_nodes: n,
            adjacency,
            node_columns,
        }
    }

    /// For each column referenced in the program, find all other columns it
    /// co-occurs with in at least one constraint.
    pub fn column_dependencies(program: &AIRProgram) -> HashMap<usize, Vec<usize>> {
        let mut deps: HashMap<usize, HashSet<usize>> = HashMap::new();
        for c in &program.constraints {
            let cols: Vec<usize> = c.referenced_columns().into_iter().collect();
            for &col in &cols {
                let entry = deps.entry(col).or_insert_with(HashSet::new);
                for &other in &cols {
                    if other != col {
                        entry.insert(other);
                    }
                }
            }
        }
        deps.into_iter()
            .map(|(k, v)| {
                let mut sorted: Vec<usize> = v.into_iter().collect();
                sorted.sort_unstable();
                (k, sorted)
            })
            .collect()
    }

    /// Check whether the AIR program is well-formed:
    /// - all referenced columns exist in the layout
    /// - no duplicate constraint names
    /// - boundary constraints have valid rows
    pub fn is_well_formed(program: &AIRProgram) -> bool {
        let num_cols = program.layout.num_columns;

        // Check referenced columns are within bounds
        for c in &program.constraints {
            for &col in &c.referenced_columns() {
                if col >= num_cols {
                    return false;
                }
            }
        }

        // Check for duplicate constraint names
        let mut names = HashSet::new();
        for c in &program.constraints {
            if !names.insert(&c.name) {
                return false;
            }
        }

        // Check boundary constraint rows are non-negative (always true for usize)
        // and that boundary constraints actually have a row set
        for c in &program.constraints {
            if c.is_boundary() && c.boundary_row.is_none() {
                return false;
            }
        }

        true
    }

    /// Return the maximum degree among transition constraints.
    pub fn max_transition_degree(program: &AIRProgram) -> usize {
        program.constraints.iter()
            .filter(|c| c.is_transition())
            .map(|c| c.degree)
            .max()
            .unwrap_or(0)
    }

    /// Return the maximum degree among boundary constraints.
    pub fn max_boundary_degree(program: &AIRProgram) -> usize {
        program.constraints.iter()
            .filter(|c| c.is_boundary())
            .map(|c| c.degree)
            .max()
            .unwrap_or(0)
    }

    /// Return sorted list of all column indices referenced by any constraint.
    pub fn active_columns(program: &AIRProgram) -> Vec<usize> {
        let mut cols: BTreeSet<usize> = BTreeSet::new();
        for c in &program.constraints {
            cols.extend(c.referenced_columns());
        }
        cols.into_iter().collect()
    }

    /// Return columns declared in the layout but not referenced by any constraint.
    pub fn unused_columns(program: &AIRProgram, layout: &TraceLayout) -> Vec<usize> {
        let active: BTreeSet<usize> = Self::active_columns(program).into_iter().collect();
        (0..layout.num_columns)
            .filter(|c| !active.contains(c))
            .collect()
    }

    /// Heuristic cost estimate for verifying the program.
    /// Based on number of constraints and their degrees.
    fn estimated_verification_cost(program: &AIRProgram) -> usize {
        program.constraints.iter()
            .map(|c| c.degree + 1)
            .sum()
    }
}

