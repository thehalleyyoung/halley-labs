// TraceLayout
// ═══════════════════════════════════════════════════════════════════════════

/// Describes the schema of an execution trace.
#[derive(Debug, Clone)]
pub struct TraceLayout {
    /// Ordered list of column descriptors.
    pub columns: Vec<TraceColumn>,
    /// Total number of columns.
    pub num_columns: usize,
    /// Indices of state columns.
    pub state_columns: Vec<usize>,
    /// Indices of input columns.
    pub input_columns: Vec<usize>,
    /// Indices of auxiliary columns.
    pub aux_columns: Vec<usize>,
    /// Indices of public columns.
    pub public_columns: Vec<usize>,
    /// Lookup: name → index.
    name_to_index: HashMap<String, usize>,
}

impl TraceLayout {
    /// Create an empty layout.
    pub fn new() -> Self {
        Self {
            columns: Vec::new(),
            num_columns: 0,
            state_columns: Vec::new(),
            input_columns: Vec::new(),
            aux_columns: Vec::new(),
            public_columns: Vec::new(),
            name_to_index: HashMap::new(),
        }
    }

    /// Build a layout appropriate for WFA execution with `num_states` state
    /// columns, plus standard input/aux/public columns.
    ///
    /// Layout:
    ///   [0]              state_id          State
    ///   [1..num_states]  state_weight_i    State
    ///   [num_states]     input_symbol      Input
    ///   [num_states+1]   accumulator       State
    ///   [num_states+2]   transition_weight Auxiliary
    ///   [num_states+3]   step_count        Public
    ///   [num_states+4]   is_accepting      Auxiliary
    ///   [num_states+5]   flag_register     Auxiliary
    pub fn from_wfa_state_count(num_states: usize) -> Self {
        let mut layout = Self::new();

        layout.add_column("state_id", ColumnType::State);

        for i in 0..num_states {
            layout.add_column(format!("state_weight_{}", i), ColumnType::State);
        }

        layout.add_column("input_symbol", ColumnType::Input);
        layout.add_column("accumulator", ColumnType::State);
        layout.add_column("transition_weight", ColumnType::Auxiliary);
        layout.add_column("step_count", ColumnType::Public);
        layout.add_column("is_accepting", ColumnType::Auxiliary);
        layout.add_column("flag_register", ColumnType::Auxiliary);

        layout
    }

    /// Look up a column index by name.
    pub fn column_index_by_name(&self, name: &str) -> Option<usize> {
        self.name_to_index.get(name).copied()
    }

    /// Append a column to the layout, returning its index.
    pub fn add_column(
        &mut self,
        name: impl Into<String>,
        column_type: ColumnType,
    ) -> usize {
        let idx = self.num_columns;
        let name_str = name.into();
        let col = TraceColumn::new(idx, name_str.clone(), column_type);
        self.columns.push(col);
        self.name_to_index.insert(name_str, idx);

        match column_type {
            ColumnType::State     => self.state_columns.push(idx),
            ColumnType::Input     => self.input_columns.push(idx),
            ColumnType::Auxiliary => self.aux_columns.push(idx),
            ColumnType::Public    => self.public_columns.push(idx),
        }

        self.num_columns += 1;
        idx
    }

    /// Validate internal consistency.
    pub fn validate(&self) -> Result<(), String> {
        if self.columns.len() != self.num_columns {
            return Err(format!(
                "Column count mismatch: {} columns but num_columns={}",
                self.columns.len(),
                self.num_columns
            ));
        }
        for (i, col) in self.columns.iter().enumerate() {
            if col.index != i {
                return Err(format!(
                    "Column '{}' has index {} but is at position {}",
                    col.name, col.index, i
                ));
            }
        }
        for &idx in &self.state_columns {
            if idx >= self.num_columns || self.columns[idx].column_type != ColumnType::State {
                return Err(format!("State column index {} is invalid", idx));
            }
        }
        for &idx in &self.input_columns {
            if idx >= self.num_columns || self.columns[idx].column_type != ColumnType::Input {
                return Err(format!("Input column index {} is invalid", idx));
            }
        }
        for &idx in &self.aux_columns {
            if idx >= self.num_columns || self.columns[idx].column_type != ColumnType::Auxiliary {
                return Err(format!("Aux column index {} is invalid", idx));
            }
        }
        for &idx in &self.public_columns {
            if idx >= self.num_columns || self.columns[idx].column_type != ColumnType::Public {
                return Err(format!("Public column index {} is invalid", idx));
            }
        }
        if self.name_to_index.len() != self.num_columns {
            return Err("Duplicate column names detected".to_string());
        }
        Ok(())
    }

    /// Return column names in order.
    pub fn column_names(&self) -> Vec<&str> {
        self.columns.iter().map(|c| c.name.as_str()).collect()
    }

    /// Number of state columns.
    pub fn state_column_count(&self) -> usize {
        self.state_columns.len()
    }

    /// Number of input columns.
    pub fn input_column_count(&self) -> usize {
        self.input_columns.len()
    }

    /// Number of auxiliary columns.
    pub fn aux_column_count(&self) -> usize {
        self.aux_columns.len()
    }

    /// Number of public columns.
    pub fn public_column_count(&self) -> usize {
        self.public_columns.len()
    }
}

impl Default for TraceLayout {
    fn default() -> Self {
        Self::new()
    }
}

