/// Evaluate all constraints at every applicable row and return a matrix
/// of evaluations: result[constraint_index][row].
pub fn evaluate_all_constraints(
    program: &AIRProgram,
    trace: &AIRTrace,
) -> Vec<Vec<GoldilocksField>> {
    let n = trace.num_rows;
    let mut result = Vec::with_capacity(program.constraints.len());

    for constraint in &program.constraints {
        let mut evals = vec![GoldilocksField::ZERO; n];
        match constraint.constraint_type {
            ConstraintType::Boundary => {
                if let Some(row) = constraint.boundary_row {
                    if row < n {
                        let tr = trace.get_row(row);
                        evals[row] = constraint.expression.evaluate_single_row(tr);
                    }
                }
            }
            ConstraintType::Transition | ConstraintType::Composition => {
                for r in 0..n.saturating_sub(1) {
                    let (cur, nxt) = trace.window_at(r);
                    evals[r] = constraint.expression.evaluate(cur, nxt);
                }
            }
            ConstraintType::Periodic => {
                let period = constraint.period.unwrap_or(1).max(1);
                for r in (0..n.saturating_sub(1)).step_by(period) {
                    let (cur, nxt) = trace.window_at(r);
                    evals[r] = constraint.expression.evaluate(cur, nxt);
                }
            }
        }
        result.push(evals);
    }
    result
}

/// Compute the maximum absolute constraint violation across all rows.
pub fn max_violation(
    program: &AIRProgram,
    trace: &AIRTrace,
) -> Option<(usize, usize, GoldilocksField)> {
    let evals = evaluate_all_constraints(program, trace);
    let mut worst: Option<(usize, usize, GoldilocksField)> = None;

    for (ci, col_evals) in evals.iter().enumerate() {
        for (r, &v) in col_evals.iter().enumerate() {
            if !v.is_zero() {
                let val = v.to_canonical();
                let is_worse = match &worst {
                    None => true,
                    Some((_, _, prev)) => val > prev.to_canonical(),
                };
                if is_worse {
                    worst = Some((ci, r, v));
                }
            }
        }
    }
    worst
}

