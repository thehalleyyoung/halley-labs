/// Build a constraint expression that checks if a value is binary (0 or 1):
///   x * (1 - x) = 0
pub fn binary_constraint(col: usize) -> SymbolicExpression {
    let x = SymbolicExpression::CurrentRow(col);
    let one = SymbolicExpression::one();
    x.clone() * (one - x)
}

/// Build a constraint that enforces a conditional update:
///   flag * (next_val - new_val) + (1 - flag) * (next_val - old_val) = 0
pub fn conditional_update(
    flag_col: usize,
    val_col: usize,
    new_val_expr: SymbolicExpression,
) -> SymbolicExpression {
    let flag = SymbolicExpression::CurrentRow(flag_col);
    let next_val = SymbolicExpression::NextRow(val_col);
    let old_val = SymbolicExpression::CurrentRow(val_col);
    let one = SymbolicExpression::one();

    flag.clone() * (next_val.clone() - new_val_expr)
        + (one - flag) * (next_val - old_val)
}

/// Build a constraint for a linear recurrence: x' = a * x + b
pub fn linear_recurrence(
    col: usize,
    a: GoldilocksField,
    b: GoldilocksField,
) -> SymbolicExpression {
    let x = SymbolicExpression::CurrentRow(col);
    let x_next = SymbolicExpression::NextRow(col);
    let a_expr = SymbolicExpression::Constant(a);
    let b_expr = SymbolicExpression::Constant(b);
    x_next - (a_expr * x + b_expr)
}

/// Build a constraint for a polynomial evaluation step:
///   acc' = acc * x + coeff
pub fn polynomial_eval_step(
    acc_col: usize,
    x_col: usize,
    coeff_col: usize,
) -> SymbolicExpression {
    let acc = SymbolicExpression::CurrentRow(acc_col);
    let acc_next = SymbolicExpression::NextRow(acc_col);
    let x = SymbolicExpression::CurrentRow(x_col);
    let coeff = SymbolicExpression::CurrentRow(coeff_col);

    acc_next - (acc * x + coeff)
}

/// Build a constraint that checks two columns are equal.
pub fn equality_constraint(col_a: usize, col_b: usize) -> SymbolicExpression {
    SymbolicExpression::CurrentRow(col_a) - SymbolicExpression::CurrentRow(col_b)
}

/// Build a "copy constraint" checking that col_a[current] = col_b[next].
pub fn copy_constraint(col_a: usize, col_b: usize) -> SymbolicExpression {
    SymbolicExpression::NextRow(col_b) - SymbolicExpression::CurrentRow(col_a)
}

/// Build a constraint that checks a column is constant (does not change).
pub fn constant_column_constraint(col: usize) -> SymbolicExpression {
    SymbolicExpression::NextRow(col) - SymbolicExpression::CurrentRow(col)
}

/// Build a constraint for a running product:
///   acc' = acc * (β - val)
pub fn running_product_constraint(
    acc_col: usize,
    val_col: usize,
    beta: GoldilocksField,
) -> SymbolicExpression {
    let acc = SymbolicExpression::CurrentRow(acc_col);
    let acc_next = SymbolicExpression::NextRow(acc_col);
    let val = SymbolicExpression::CurrentRow(val_col);
    let beta_expr = SymbolicExpression::Constant(beta);

    acc_next - acc * (beta_expr - val)
}

/// Build a WFA transition constraint.
pub fn wfa_transition_constraint(
    _state_col: usize,
    acc_col: usize,
    weight_col: usize,
) -> Vec<SymbolicExpression> {
    let acc = SymbolicExpression::CurrentRow(acc_col);
    let acc_next = SymbolicExpression::NextRow(acc_col);
    let weight = SymbolicExpression::CurrentRow(weight_col);

    let acc_update = acc_next - acc - weight;

    vec![acc_update]
}

