/// Utility for debugging execution traces against AIR constraints.
/// Checks each constraint at each applicable row and reports violations.
pub struct TraceDebugger;

impl TraceDebugger {
    /// Check all constraints against the trace, returning all violations.
    pub fn check_trace(
        program: &AIRProgram,
        trace: &AIRTrace,
    ) -> Vec<ConstraintViolation> {
        let mut violations = Vec::new();

        for constraint in &program.constraints {
            match constraint.constraint_type {
                ConstraintType::Boundary => {
                    if let Some(row) = constraint.boundary_row {
                        if row < trace.num_rows {
                            let tr = trace.get_row(row);
                            let val = constraint.expression.evaluate_single_row(tr);
                            if !val.is_zero() {
                                violations.push(ConstraintViolation {
                                    constraint_name: constraint.name.clone(),
                                    row,
                                    expected_zero: GoldilocksField::ZERO,
                                    actual_value: val,
                                    constraint_type: constraint.constraint_type,
                                });
                            }
                        }
                    }
                }
                ConstraintType::Transition => {
                    for r in 0..trace.num_rows.saturating_sub(1) {
                        let current = trace.get_row(r);
                        let next = trace.get_row(r + 1);
                        let val = constraint.expression.evaluate(current, next);
                        if !val.is_zero() {
                            violations.push(ConstraintViolation {
                                constraint_name: constraint.name.clone(),
                                row: r,
                                expected_zero: GoldilocksField::ZERO,
                                actual_value: val,
                                constraint_type: constraint.constraint_type,
                            });
                        }
                    }
                }
                ConstraintType::Periodic => {
                    let period = constraint.period.unwrap_or(1);
                    for r in 0..trace.num_rows.saturating_sub(1) {
                        if period > 0 && r % period == 0 {
                            let current = trace.get_row(r);
                            let next = trace.get_row(r + 1);
                            let val = constraint.expression.evaluate(current, next);
                            if !val.is_zero() {
                                violations.push(ConstraintViolation {
                                    constraint_name: constraint.name.clone(),
                                    row: r,
                                    expected_zero: GoldilocksField::ZERO,
                                    actual_value: val,
                                    constraint_type: constraint.constraint_type,
                                });
                            }
                        }
                    }
                }
                ConstraintType::Composition => {
                    // Composition constraints are typically not checked individually
                }
            }
        }

        violations
    }

    /// Return only the first violation found (useful for quick debugging).
    pub fn first_violation(
        program: &AIRProgram,
        trace: &AIRTrace,
    ) -> Option<ConstraintViolation> {
        // Check constraints in order, return the first violation
        for constraint in &program.constraints {
            match constraint.constraint_type {
                ConstraintType::Boundary => {
                    if let Some(row) = constraint.boundary_row {
                        if row < trace.num_rows {
                            let tr = trace.get_row(row);
                            let val = constraint.expression.evaluate_single_row(tr);
                            if !val.is_zero() {
                                return Some(ConstraintViolation {
                                    constraint_name: constraint.name.clone(),
                                    row,
                                    expected_zero: GoldilocksField::ZERO,
                                    actual_value: val,
                                    constraint_type: constraint.constraint_type,
                                });
                            }
                        }
                    }
                }
                ConstraintType::Transition => {
                    for r in 0..trace.num_rows.saturating_sub(1) {
                        let current = trace.get_row(r);
                        let next = trace.get_row(r + 1);
                        let val = constraint.expression.evaluate(current, next);
                        if !val.is_zero() {
                            return Some(ConstraintViolation {
                                constraint_name: constraint.name.clone(),
                                row: r,
                                expected_zero: GoldilocksField::ZERO,
                                actual_value: val,
                                constraint_type: constraint.constraint_type,
                            });
                        }
                    }
                }
                _ => {}
            }
        }
        None
    }

    /// Generate a human-readable summary of all violations.
    pub fn violation_summary(violations: &[ConstraintViolation]) -> String {
        if violations.is_empty() {
            return "No violations found.".to_string();
        }
        let mut summary = format!("{} violation(s) found:\n", violations.len());
        // Group by constraint name
        let mut by_name: HashMap<&str, Vec<usize>> = HashMap::new();
        for v in violations {
            by_name.entry(&v.constraint_name).or_default().push(v.row);
        }
        let mut names: Vec<&&str> = by_name.keys().collect();
        names.sort();
        for name in names {
            let rows = &by_name[name];
            if rows.len() <= 5 {
                summary.push_str(&format!("  '{}': rows {:?}\n", name, rows));
            } else {
                summary.push_str(&format!(
                    "  '{}': {} violations (first 5 rows: {:?})\n",
                    name, rows.len(), &rows[..5]
                ));
            }
        }
        summary
    }

    /// Suggest a fix for a constraint violation based on the trace values.
    pub fn suggest_fix(
        violation: &ConstraintViolation,
        trace: &AIRTrace,
    ) -> String {
        match violation.constraint_type {
            ConstraintType::Boundary => {
                let row = violation.row;
                if row < trace.num_rows {
                    let row_vals = trace.get_row(row);
                    format!(
                        "Boundary constraint '{}' violated at row {}. \
                         Current row values: {:?}. \
                         Adjust initial values so the constraint evaluates to 0 \
                         (currently evaluates to {}).",
                        violation.constraint_name, row,
                        &row_vals[..row_vals.len().min(8)],
                        violation.actual_value
                    )
                } else {
                    format!(
                        "Boundary constraint '{}' references row {} which is \
                         beyond the trace length {}.",
                        violation.constraint_name, row, trace.num_rows
                    )
                }
            }
            ConstraintType::Transition => {
                let row = violation.row;
                let current = trace.get_row(row);
                let next = if row + 1 < trace.num_rows {
                    trace.get_row(row + 1)
                } else {
                    current
                };
                format!(
                    "Transition constraint '{}' violated at row {}→{}. \
                     Current: {:?}, Next: {:?}. \
                     Modify the trace generation so the transition expression \
                     evaluates to 0 (currently {}).",
                    violation.constraint_name, row, row + 1,
                    &current[..current.len().min(8)],
                    &next[..next.len().min(8)],
                    violation.actual_value
                )
            }
            _ => {
                format!(
                    "Constraint '{}' ({}) violated at row {}. \
                     Value: {}. Review the constraint definition.",
                    violation.constraint_name, violation.constraint_type,
                    violation.row, violation.actual_value
                )
            }
        }
    }
}

