/// Build a permutation-check AIR.
pub fn permutation_check_air() -> AIRProgram {
    let mut layout = TraceLayout::new();
    layout.add_column("original", ColumnType::State);
    layout.add_column("permuted", ColumnType::State);
    layout.add_column("acc_orig", ColumnType::Auxiliary);
    layout.add_column("acc_perm", ColumnType::Auxiliary);

    let mut program = AIRProgram::new("permutation_check", layout);

    program.add_boundary_constraint(2, 0, GoldilocksField::ZERO);
    program.add_boundary_constraint(3, 0, GoldilocksField::ZERO);

    // acc_orig' = acc_orig + original
    let t1 = SymbolicExpression::NextRow(2)
        - SymbolicExpression::CurrentRow(2)
        - SymbolicExpression::CurrentRow(0);
    program.add_transition_constraint("acc_orig_update", t1);

    // acc_perm' = acc_perm + permuted
    let t2 = SymbolicExpression::NextRow(3)
        - SymbolicExpression::CurrentRow(3)
        - SymbolicExpression::CurrentRow(1);
    program.add_transition_constraint("acc_perm_update", t2);

    program
}

/// Build a "power-sum" AIR that computes running sums of powers.
///
/// Columns: [x, x_pow, running_sum]
pub fn power_sum_air() -> AIRProgram {
    let mut layout = TraceLayout::new();
    layout.add_column("x", ColumnType::Input);
    layout.add_column("x_pow", ColumnType::State);
    layout.add_column("running_sum", ColumnType::State);

    let mut program = AIRProgram::new("power_sum", layout);

    program.add_boundary_constraint(1, 0, GoldilocksField::ONE);
    program.add_boundary_constraint(2, 0, GoldilocksField::ZERO);

    // x_pow' = x_pow * x
    let t1 = SymbolicExpression::NextRow(1)
        - SymbolicExpression::CurrentRow(1) * SymbolicExpression::CurrentRow(0);
    program.add_transition_constraint("x_pow_update", t1);

    // running_sum' = running_sum + x_pow
    let t2 = SymbolicExpression::NextRow(2)
        - SymbolicExpression::CurrentRow(2)
        - SymbolicExpression::CurrentRow(1);
    program.add_transition_constraint("running_sum_update", t2);

    program
}

/// Generate a power-sum trace for a given base value.
pub fn power_sum_trace(x_val: u64, num_steps: usize) -> AIRTrace {
    let mut layout = TraceLayout::new();
    layout.add_column("x", ColumnType::Input);
    layout.add_column("x_pow", ColumnType::State);
    layout.add_column("running_sum", ColumnType::State);

    let x = GoldilocksField::new(x_val);
    let mut trace = AIRTrace::new(num_steps, layout);
    let mut x_pow = GoldilocksField::ONE;
    let mut running_sum = GoldilocksField::ZERO;

    for r in 0..num_steps {
        trace.set(r, 0, x);
        trace.set(r, 1, x_pow);
        trace.set(r, 2, running_sum);
        running_sum = running_sum + x_pow;
        x_pow = x_pow * x;
    }

    trace
}

/// Build a "range check" AIR that verifies all values in a column are
/// in [0, 2^k) by checking bit decomposition.
pub fn range_check_air(num_bits: usize) -> AIRProgram {
    let mut layout = TraceLayout::new();
    layout.add_column("value", ColumnType::State);
    for i in 0..num_bits {
        layout.add_column(format!("bit_{}", i), ColumnType::Auxiliary);
    }

    let mut program = AIRProgram::new("range_check", layout);

    for i in 0..num_bits {
        let bit_col = i + 1;
        let bit = SymbolicExpression::CurrentRow(bit_col);
        let one = SymbolicExpression::one();
        let boolean_check = bit.clone() * (one - bit);
        program.add_transition_constraint(
            format!("bit_{}_boolean", i),
            boolean_check,
        );
    }

    // value = Σ bit_i * 2^i
    let mut recomp = SymbolicExpression::CurrentRow(0);
    for i in 0..num_bits {
        let bit_col = i + 1;
        let two_pow_i = SymbolicExpression::constant(1u64 << i);
        let bit = SymbolicExpression::CurrentRow(bit_col);
        recomp = recomp - bit * two_pow_i;
    }
    program.add_transition_constraint("bit_decomposition", recomp);

    program
}

/// Generate a range-check trace.
pub fn range_check_trace(values: &[u64], num_bits: usize) -> AIRTrace {
    let num_steps = values.len();
    let mut layout = TraceLayout::new();
    layout.add_column("value", ColumnType::State);
    for i in 0..num_bits {
        layout.add_column(format!("bit_{}", i), ColumnType::Auxiliary);
    }

    let mut trace = AIRTrace::new(num_steps, layout);
    for (r, &v) in values.iter().enumerate() {
        trace.set(r, 0, GoldilocksField::new(v));
        for i in 0..num_bits {
            let bit = (v >> i) & 1;
            trace.set(r, i + 1, GoldilocksField::new(bit));
        }
    }
    trace
}

/// Build a memory-consistency AIR using a sorted-column approach.
pub fn memory_consistency_air() -> AIRProgram {
    let mut layout = TraceLayout::new();
    layout.add_column("addr", ColumnType::State);
    layout.add_column("value", ColumnType::State);
    layout.add_column("addr_sorted", ColumnType::Auxiliary);
    layout.add_column("value_sorted", ColumnType::Auxiliary);
    layout.add_column("is_same_addr", ColumnType::Auxiliary);

    let mut program = AIRProgram::new("memory_consistency", layout);

    let flag = SymbolicExpression::CurrentRow(4);
    let bool_check = flag.clone() * (SymbolicExpression::one() - flag.clone());
    program.add_transition_constraint("is_same_addr_boolean", bool_check);

    let addr_eq = flag.clone()
        * (SymbolicExpression::NextRow(2) - SymbolicExpression::CurrentRow(2));
    program.add_transition_constraint("addr_continuity", addr_eq);

    let val_continuity = flag
        * (SymbolicExpression::NextRow(3) - SymbolicExpression::CurrentRow(3));
    program.add_transition_constraint("value_continuity", val_continuity);

    program
}

/// Build a multi-register AIR simulating a simple processor.
pub fn processor_air() -> AIRProgram {
    let mut layout = TraceLayout::new();
    layout.add_column("pc", ColumnType::State);
    layout.add_column("reg_a", ColumnType::State);
    layout.add_column("reg_b", ColumnType::State);
    layout.add_column("reg_c", ColumnType::State);
    layout.add_column("opcode", ColumnType::Input);
    layout.add_column("flag", ColumnType::Auxiliary);

    let mut program = AIRProgram::new("processor", layout);

    program.add_boundary_constraint(0, 0, GoldilocksField::ZERO);

    let pc_incr = SymbolicExpression::NextRow(0)
        - SymbolicExpression::CurrentRow(0)
        - SymbolicExpression::one();
    program.add_transition_constraint("pc_increment", pc_incr);

    let flag = SymbolicExpression::CurrentRow(5);
    let flag_bool = flag.clone() * (SymbolicExpression::one() - flag);
    program.add_transition_constraint("flag_boolean", flag_bool);

    program
}

/// Verify that a trace satisfies an AIR program and return a human-readable
/// report.
pub fn verify_and_report(program: &AIRProgram, trace: &AIRTrace) -> String {
    let failures = program.verify_trace(trace);
    if failures.is_empty() {
        format!(
            "✓ All {} constraints satisfied across {} rows.",
            program.constraint_count(),
            trace.num_rows
        )
    } else {
        let mut report = format!(
            "✗ {} constraint violations found:\n",
            failures.len()
        );
        for (ci, row) in &failures {
            let name = &program.constraints[*ci].name;
            let ctype = &program.constraints[*ci].constraint_type;
            report.push_str(&format!("  - Constraint '{}' ({}) failed at row {}\n", name, ctype, row));
        }
        report
    }
}

/// Compute the composition polynomial for an AIR program and verify that
/// it is identically zero when the trace is valid.
pub fn verify_composition(
    program: &AIRProgram,
    trace: &AIRTrace,
    alphas: &[GoldilocksField],
) -> bool {
    let comp = program.composition_polynomial(trace, alphas);
    comp.iter().all(|v| v.is_zero())
}

/// Interpolate a column of field values on roots of unity and return
/// the coefficient polynomial.
pub fn interpolate_column(
    column: &[GoldilocksField],
) -> Vec<GoldilocksField> {
    let n = column.len();
    if n == 0 {
        return vec![];
    }
    if !n.is_power_of_two() {
        let xs: Vec<GoldilocksField> = (1..=n)
            .map(|i| GoldilocksField::new(i as u64))
            .collect();
        return GoldilocksField::lagrange_interpolation(&xs, column);
    }
    let omega = GoldilocksField::root_of_unity(n);
    let xs: Vec<GoldilocksField> = (0..n)
        .map(|i| omega.pow(i as u64))
        .collect();
    GoldilocksField::lagrange_interpolation(&xs, column)
}

/// Evaluate a polynomial at multiple points.
pub fn evaluate_polynomial_batch(
    coeffs: &[GoldilocksField],
    points: &[GoldilocksField],
) -> Vec<GoldilocksField> {
    points
        .iter()
        .map(|&x| GoldilocksField::eval_poly(coeffs, x))
        .collect()
}

/// Compute the quotient polynomial: q(x) = p(x) / z(x).
pub fn compute_quotient(
    trace_poly: &[GoldilocksField],
    zerofier: &[GoldilocksField],
) -> (Vec<GoldilocksField>, Vec<GoldilocksField>) {
    GoldilocksField::poly_div(trace_poly, zerofier)
}

/// Build the composition domain: a coset of the trace domain.
pub fn build_composition_domain(
    trace_len: usize,
    blowup_factor: usize,
) -> Vec<GoldilocksField> {
    let extended_len = trace_len * blowup_factor;
    if !extended_len.is_power_of_two() {
        return (0..extended_len)
            .map(|i| GoldilocksField::new(i as u64 + 1))
            .collect();
    }
    let omega = GoldilocksField::root_of_unity(extended_len);
    let shift = GoldilocksField::new(7);
    (0..extended_len)
        .map(|i| shift * omega.pow(i as u64))
        .collect()
}

