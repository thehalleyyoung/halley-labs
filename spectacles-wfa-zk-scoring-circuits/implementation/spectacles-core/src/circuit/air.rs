/// Combines multiple constraint evaluations into a single value via
/// random linear combination.
///
/// Given constraints c_0, c_1, …, c_{k-1} and challenges α_0, …, α_{k-1}
/// the composed value at a point is:
///
///   C = Σ_i  α_i · c_i
#[derive(Debug, Clone)]
pub struct ConstraintComposer {
    /// Number of constraints being composed.
    num_constraints: usize,
    /// Random challenge coefficients.
    alphas: Vec<GoldilocksField>,
    /// Power-of-alpha coefficients for batching via powers of a single α.
    alpha_powers: Vec<GoldilocksField>,
}

impl ConstraintComposer {
    /// Create a composer with explicit per-constraint challenges.
    pub fn new(alphas: Vec<GoldilocksField>) -> Self {
        let num_constraints = alphas.len();
        let alpha_powers = if !alphas.is_empty() {
            let base = alphas[0];
            (0..num_constraints)
                .map(|i| base.pow(i as u64))
                .collect()
        } else {
            Vec::new()
        };
        Self {
            num_constraints,
            alphas,
            alpha_powers,
        }
    }

    /// Create a composer that uses powers of a single α.
    pub fn from_single_alpha(alpha: GoldilocksField, num_constraints: usize) -> Self {
        let alphas: Vec<GoldilocksField> = (0..num_constraints)
            .map(|i| alpha.pow(i as u64))
            .collect();
        let alpha_powers = alphas.clone();
        Self {
            num_constraints,
            alphas,
            alpha_powers,
        }
    }

    /// Compose evaluated constraint values into a single field element.
    pub fn compose(
        &self,
        evaluations: &[GoldilocksField],
    ) -> GoldilocksField {
        let mut result = GoldilocksField::ZERO;
        let len = evaluations.len().min(self.alphas.len());
        for i in 0..len {
            result = result + self.alphas[i] * evaluations[i];
        }
        result
    }

    /// Compose all constraints evaluated at a specific row of a trace.
    pub fn evaluate_composition_at(
        &self,
        constraints: &[AIRConstraint],
        trace: &AIRTrace,
        row: usize,
    ) -> GoldilocksField {
        let (cur, nxt) = trace.window_at(row);
        let mut result = GoldilocksField::ZERO;
        for (i, constraint) in constraints.iter().enumerate() {
            if i >= self.alphas.len() {
                break;
            }
            let eval = constraint.expression.evaluate(cur, nxt);
            result = result + self.alphas[i] * eval;
        }
        result
    }

    /// Compose using the alpha-power scheme (batching).
    pub fn compose_with_powers(
        &self,
        evaluations: &[GoldilocksField],
    ) -> GoldilocksField {
        let mut result = GoldilocksField::ZERO;
        let len = evaluations.len().min(self.alpha_powers.len());
        for i in 0..len {
            result = result + self.alpha_powers[i] * evaluations[i];
        }
        result
    }

    /// Compute the full composition polynomial over all rows of a trace.
    pub fn compose_over_trace(
        &self,
        constraints: &[AIRConstraint],
        trace: &AIRTrace,
    ) -> Vec<GoldilocksField> {
        let n = trace.num_rows;
        let mut result = vec![GoldilocksField::ZERO; n];
        for r in 0..n.saturating_sub(1) {
            result[r] = self.evaluate_composition_at(constraints, trace, r);
        }
        result
    }

    /// Return the number of constraints.
    pub fn num_constraints(&self) -> usize {
        self.num_constraints
    }

    /// Return a reference to the alpha challenges.
    pub fn alphas(&self) -> &[GoldilocksField] {
        &self.alphas
    }

    /// Deep-compose: combine constraint evaluations with degree adjustment.
    pub fn deep_compose(
        &self,
        evaluations: &[GoldilocksField],
        degrees: &[usize],
        max_degree: usize,
        x: GoldilocksField,
    ) -> GoldilocksField {
        let mut result = GoldilocksField::ZERO;
        let len = evaluations.len().min(self.alphas.len()).min(degrees.len());
        for i in 0..len {
            let degree_adjustment = max_degree.saturating_sub(degrees[i]);
            let adjustment_factor = x.pow(degree_adjustment as u64);
            result = result + self.alphas[i] * evaluations[i] * adjustment_factor;
        }
        result
    }
}

