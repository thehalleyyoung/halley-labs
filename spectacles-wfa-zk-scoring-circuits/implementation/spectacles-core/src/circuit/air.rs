/// Classification of a trace column.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
pub enum ColumnType {
    /// Core state columns (e.g. WFA state id, accumulator).
    State,
    /// Input columns (symbols fed to the automaton).
    Input,
    /// Auxiliary / witness columns generated by the prover.
    Auxiliary,
    /// Public-input columns whose values are known to the verifier.
    Public,
}

impl fmt::Display for ColumnType {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        match self {
            ColumnType::State     => write!(f, "State"),
            ColumnType::Input     => write!(f, "Input"),
            ColumnType::Auxiliary => write!(f, "Auxiliary"),
            ColumnType::Public    => write!(f, "Public"),
        }
    }
}

// ═══════════════════════════════════════════════════════════════════════════
// SymbolicExpression
// ═══════════════════════════════════════════════════════════════════════════

/// An arithmetic-circuit AST over field elements.
///
/// Variables are addressed by (column_index, row_offset) where row_offset
/// is typically 0 ("current row") or 1 ("next row").  Convenience variants
/// `CurrentRow` and `NextRow` exist for clarity.
#[derive(Debug, Clone, PartialEq, Eq, Hash)]
pub enum SymbolicExpression {
    /// A field constant.
    Constant(GoldilocksField),
    /// A trace cell: column `col`, relative row offset `row_offset`.
    Variable { col: usize, row_offset: i32 },
    /// Sum of two sub-expressions.
    Add(Box<SymbolicExpression>, Box<SymbolicExpression>),
    /// Product of two sub-expressions.
    Mul(Box<SymbolicExpression>, Box<SymbolicExpression>),
    /// Difference of two sub-expressions.
    Sub(Box<SymbolicExpression>, Box<SymbolicExpression>),
    /// Additive negation.
    Neg(Box<SymbolicExpression>),
    /// Raise a sub-expression to a constant power.
    Pow(Box<SymbolicExpression>, u64),
    /// Shorthand for Variable { col, row_offset: 0 }.
    CurrentRow(usize),
    /// Shorthand for Variable { col, row_offset: 1 }.
    NextRow(usize),
}

// ── Display ──────────────────────────────────────────────────────────────

impl fmt::Display for SymbolicExpression {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        match self {
            SymbolicExpression::Constant(c) => write!(f, "{}", c),
            SymbolicExpression::Variable { col, row_offset } => {
                write!(f, "x[{}][{}]", col, row_offset)
            }
            SymbolicExpression::Add(a, b) => write!(f, "({} + {})", a, b),
            SymbolicExpression::Mul(a, b) => write!(f, "({} * {})", a, b),
            SymbolicExpression::Sub(a, b) => write!(f, "({} - {})", a, b),
            SymbolicExpression::Neg(a) => write!(f, "(-{})", a),
            SymbolicExpression::Pow(base, exp) => write!(f, "({})^{}", base, exp),
            SymbolicExpression::CurrentRow(col) => write!(f, "cur[{}]", col),
            SymbolicExpression::NextRow(col) => write!(f, "nxt[{}]", col),
        }
    }
}

// ── Constructors & helpers ───────────────────────────────────────────────

impl SymbolicExpression {
    /// Constant zero.
    pub fn zero() -> Self {
        SymbolicExpression::Constant(GoldilocksField::ZERO)
    }

    /// Constant one.
    pub fn one() -> Self {
        SymbolicExpression::Constant(GoldilocksField::ONE)
    }

    /// Create a constant expression from a u64.
    pub fn constant(v: u64) -> Self {
        SymbolicExpression::Constant(GoldilocksField::new(v))
    }

    /// Create a constant from a field element.
    pub fn constant_field(v: GoldilocksField) -> Self {
        SymbolicExpression::Constant(v)
    }

    /// Variable at (col, row_offset).
    pub fn var(col: usize, row_offset: i32) -> Self {
        SymbolicExpression::Variable { col, row_offset }
    }

    /// Current-row access to `col`.
    pub fn cur(col: usize) -> Self {
        SymbolicExpression::CurrentRow(col)
    }

    /// Next-row access to `col`.
    pub fn nxt(col: usize) -> Self {
        SymbolicExpression::NextRow(col)
    }

    // ── Evaluation ───────────────────────────────────────────────────────

    /// Evaluate the expression given concrete values for the current and next
    /// trace rows.  Both slices are indexed by column.
    ///
    /// For `Variable { row_offset: 0 }` or `CurrentRow` we read from
    /// `trace_row`; for offset 1 / `NextRow` we read from `next_row`.
    /// Offsets outside {0, 1} are treated as 0 (current) when negative and
    /// 1 (next) when positive – sufficient for degree-2 transition
    /// constraints which is the common case.
    pub fn evaluate(
        &self,
        trace_row: &[GoldilocksField],
        next_row: &[GoldilocksField],
    ) -> GoldilocksField {
        match self {
            SymbolicExpression::Constant(c) => *c,

            SymbolicExpression::Variable { col, row_offset } => {
                if *row_offset <= 0 {
                    trace_row[*col]
                } else {
                    next_row[*col]
                }
            }

            SymbolicExpression::CurrentRow(col) => trace_row[*col],
            SymbolicExpression::NextRow(col) => next_row[*col],

            SymbolicExpression::Add(a, b) => {
                a.evaluate(trace_row, next_row) + b.evaluate(trace_row, next_row)
            }
            SymbolicExpression::Mul(a, b) => {
                a.evaluate(trace_row, next_row) * b.evaluate(trace_row, next_row)
            }
            SymbolicExpression::Sub(a, b) => {
                a.evaluate(trace_row, next_row) - b.evaluate(trace_row, next_row)
            }
            SymbolicExpression::Neg(a) => -a.evaluate(trace_row, next_row),

            SymbolicExpression::Pow(base, exp) => {
                base.evaluate(trace_row, next_row).pow(*exp)
            }
        }
    }

    /// Evaluate the expression using only `trace_row` (next_row is treated
    /// as all-zeros).  Useful for boundary constraints that reference a
    /// single row.
    pub fn evaluate_single_row(&self, trace_row: &[GoldilocksField]) -> GoldilocksField {
        let zero_row = vec![GoldilocksField::ZERO; trace_row.len()];
        self.evaluate(trace_row, &zero_row)
    }

    // ── Algebraic degree ─────────────────────────────────────────────────

    /// Return the total algebraic degree of the expression where each
    /// variable contributes degree 1 and constants contribute degree 0.
    pub fn degree(&self) -> usize {
        match self {
            SymbolicExpression::Constant(_) => 0,
            SymbolicExpression::Variable { .. }
            | SymbolicExpression::CurrentRow(_)
            | SymbolicExpression::NextRow(_) => 1,

            SymbolicExpression::Add(a, b)
            | SymbolicExpression::Sub(a, b) => {
                a.degree().max(b.degree())
            }

            SymbolicExpression::Mul(a, b) => a.degree() + b.degree(),

            SymbolicExpression::Neg(a) => a.degree(),

            SymbolicExpression::Pow(base, exp) => {
                base.degree() * (*exp as usize)
            }
        }
    }

    // ── Simplification ───────────────────────────────────────────────────

    /// Perform a single bottom-up simplification pass:
    ///   0 + x → x,  x + 0 → x
    ///   0 * x → 0,  x * 0 → 0
    ///   1 * x → x,  x * 1 → x
    ///   x - 0 → x
    ///   --x → x
    ///   x^0 → 1,  x^1 → x
    ///   constant folding for Constant ⊕ Constant
    pub fn simplify(&self) -> Self {
        match self {
            SymbolicExpression::Constant(_) => self.clone(),
            SymbolicExpression::Variable { .. }
            | SymbolicExpression::CurrentRow(_)
            | SymbolicExpression::NextRow(_) => self.clone(),

            SymbolicExpression::Add(a, b) => {
                let sa = a.simplify();
                let sb = b.simplify();
                // constant fold
                if let (SymbolicExpression::Constant(ca), SymbolicExpression::Constant(cb)) =
                    (&sa, &sb)
                {
                    return SymbolicExpression::Constant(*ca + *cb);
                }
                // 0 + x
                if sa.is_constant() && sa.constant_value() == Some(GoldilocksField::ZERO) {
                    return sb;
                }
                // x + 0
                if sb.is_constant() && sb.constant_value() == Some(GoldilocksField::ZERO) {
                    return sa;
                }
                SymbolicExpression::Add(Box::new(sa), Box::new(sb))
            }

            SymbolicExpression::Sub(a, b) => {
                let sa = a.simplify();
                let sb = b.simplify();
                if let (SymbolicExpression::Constant(ca), SymbolicExpression::Constant(cb)) =
                    (&sa, &sb)
                {
                    return SymbolicExpression::Constant(*ca - *cb);
                }
                // x - 0
                if sb.is_constant() && sb.constant_value() == Some(GoldilocksField::ZERO) {
                    return sa;
                }
                // 0 - x → -x
                if sa.is_constant() && sa.constant_value() == Some(GoldilocksField::ZERO) {
                    return SymbolicExpression::Neg(Box::new(sb));
                }
                SymbolicExpression::Sub(Box::new(sa), Box::new(sb))
            }

            SymbolicExpression::Mul(a, b) => {
                let sa = a.simplify();
                let sb = b.simplify();
                if let (SymbolicExpression::Constant(ca), SymbolicExpression::Constant(cb)) =
                    (&sa, &sb)
                {
                    return SymbolicExpression::Constant(*ca * *cb);
                }
                // 0 * x or x * 0
                if (sa.is_constant() && sa.constant_value() == Some(GoldilocksField::ZERO))
                    || (sb.is_constant() && sb.constant_value() == Some(GoldilocksField::ZERO))
                {
                    return SymbolicExpression::Constant(GoldilocksField::ZERO);
                }
                // 1 * x
                if sa.is_constant() && sa.constant_value() == Some(GoldilocksField::ONE) {
                    return sb;
                }
                // x * 1
                if sb.is_constant() && sb.constant_value() == Some(GoldilocksField::ONE) {
                    return sa;
                }
                SymbolicExpression::Mul(Box::new(sa), Box::new(sb))
            }

            SymbolicExpression::Neg(a) => {
                let sa = a.simplify();
                if let SymbolicExpression::Constant(c) = &sa {
                    return SymbolicExpression::Constant(-*c);
                }
                // double negation
                if let SymbolicExpression::Neg(inner) = sa {
                    return *inner;
                }
                SymbolicExpression::Neg(Box::new(sa))
            }

            SymbolicExpression::Pow(base, exp) => {
                let sb = base.simplify();
                if *exp == 0 {
                    return SymbolicExpression::Constant(GoldilocksField::ONE);
                }
                if *exp == 1 {
                    return sb;
                }
                if let SymbolicExpression::Constant(c) = &sb {
                    return SymbolicExpression::Constant(c.pow(*exp));
                }
                SymbolicExpression::Pow(Box::new(sb), *exp)
            }
        }
    }

    /// Repeatedly simplify until fixed-point (max 32 iterations).
    pub fn simplify_full(&self) -> Self {
        let mut cur = self.clone();
        for _ in 0..32 {
            let next = cur.simplify();
            if next == cur {
                break;
            }
            cur = next;
        }
        cur
    }

    // ── Substitution ─────────────────────────────────────────────────────

    /// Replace every occurrence of `Variable { col, row_offset }` (and the
    /// equivalent `CurrentRow(col)` when `row_offset == 0`, `NextRow(col)`
    /// when `row_offset == 1`) with `replacement`.
    pub fn substitute(
        &self,
        target_col: usize,
        target_offset: i32,
        replacement: &SymbolicExpression,
    ) -> Self {
        match self {
            SymbolicExpression::Constant(_) => self.clone(),

            SymbolicExpression::Variable { col, row_offset }
                if *col == target_col && *row_offset == target_offset =>
            {
                replacement.clone()
            }
            SymbolicExpression::Variable { .. } => self.clone(),

            SymbolicExpression::CurrentRow(col) if *col == target_col && target_offset == 0 => {
                replacement.clone()
            }
            SymbolicExpression::CurrentRow(_) => self.clone(),

            SymbolicExpression::NextRow(col) if *col == target_col && target_offset == 1 => {
                replacement.clone()
            }
            SymbolicExpression::NextRow(_) => self.clone(),

            SymbolicExpression::Add(a, b) => SymbolicExpression::Add(
                Box::new(a.substitute(target_col, target_offset, replacement)),
                Box::new(b.substitute(target_col, target_offset, replacement)),
            ),
            SymbolicExpression::Mul(a, b) => SymbolicExpression::Mul(
                Box::new(a.substitute(target_col, target_offset, replacement)),
                Box::new(b.substitute(target_col, target_offset, replacement)),
            ),
            SymbolicExpression::Sub(a, b) => SymbolicExpression::Sub(
                Box::new(a.substitute(target_col, target_offset, replacement)),
                Box::new(b.substitute(target_col, target_offset, replacement)),
            ),
            SymbolicExpression::Neg(a) => SymbolicExpression::Neg(
                Box::new(a.substitute(target_col, target_offset, replacement)),
            ),
            SymbolicExpression::Pow(base, exp) => SymbolicExpression::Pow(
                Box::new(base.substitute(target_col, target_offset, replacement)),
                *exp,
            ),
        }
    }

    /// Substitute all variables using a map from `(col, row_offset)` to an
    /// expression.
    pub fn substitute_all(
        &self,
        substitutions: &HashMap<(usize, i32), SymbolicExpression>,
    ) -> Self {
        match self {
            SymbolicExpression::Constant(_) => self.clone(),

            SymbolicExpression::Variable { col, row_offset } => substitutions
                .get(&(*col, *row_offset))
                .cloned()
                .unwrap_or_else(|| self.clone()),

            SymbolicExpression::CurrentRow(col) => substitutions
                .get(&(*col, 0))
                .cloned()
                .unwrap_or_else(|| self.clone()),

            SymbolicExpression::NextRow(col) => substitutions
                .get(&(*col, 1))
                .cloned()
                .unwrap_or_else(|| self.clone()),

            SymbolicExpression::Add(a, b) => SymbolicExpression::Add(
                Box::new(a.substitute_all(substitutions)),
                Box::new(b.substitute_all(substitutions)),
            ),
            SymbolicExpression::Mul(a, b) => SymbolicExpression::Mul(
                Box::new(a.substitute_all(substitutions)),
                Box::new(b.substitute_all(substitutions)),
            ),
            SymbolicExpression::Sub(a, b) => SymbolicExpression::Sub(
                Box::new(a.substitute_all(substitutions)),
                Box::new(b.substitute_all(substitutions)),
            ),
            SymbolicExpression::Neg(a) => {
                SymbolicExpression::Neg(Box::new(a.substitute_all(substitutions)))
            }
            SymbolicExpression::Pow(base, exp) => {
                SymbolicExpression::Pow(Box::new(base.substitute_all(substitutions)), *exp)
            }
        }
    }

    // ── Polynomial form ──────────────────────────────────────────────────

    /// Convert the expression to an equivalent univariate polynomial in
    /// terms of a single variable index `var_col` at offset 0.  All other
    /// variables and constants are folded into the coefficients.
    ///
    /// Returns the coefficient vector (index = degree).  If the expression
    /// contains variables other than `var_col` at offset 0 the result is an
    /// approximation that treats them as constants (evaluated at the
    /// supplied `env` values).
    pub fn to_polynomial_form(
        &self,
        var_col: usize,
        env: &[GoldilocksField],
    ) -> Vec<GoldilocksField> {
        match self {
            SymbolicExpression::Constant(c) => vec![*c],

            SymbolicExpression::Variable { col, row_offset } => {
                if *col == var_col && *row_offset == 0 {
                    vec![GoldilocksField::ZERO, GoldilocksField::ONE]
                } else {
                    let val = if *col < env.len() { env[*col] } else { GoldilocksField::ZERO };
                    vec![val]
                }
            }

            SymbolicExpression::CurrentRow(col) => {
                if *col == var_col {
                    vec![GoldilocksField::ZERO, GoldilocksField::ONE]
                } else {
                    let val = if *col < env.len() { env[*col] } else { GoldilocksField::ZERO };
                    vec![val]
                }
            }

            SymbolicExpression::NextRow(col) => {
                let val = if *col < env.len() { env[*col] } else { GoldilocksField::ZERO };
                vec![val]
            }

            SymbolicExpression::Add(a, b) => {
                let pa = a.to_polynomial_form(var_col, env);
                let pb = b.to_polynomial_form(var_col, env);
                GoldilocksField::poly_add(&pa, &pb)
            }

            SymbolicExpression::Sub(a, b) => {
                let pa = a.to_polynomial_form(var_col, env);
                let pb = b.to_polynomial_form(var_col, env);
                GoldilocksField::poly_sub(&pa, &pb)
            }

            SymbolicExpression::Mul(a, b) => {
                let pa = a.to_polynomial_form(var_col, env);
                let pb = b.to_polynomial_form(var_col, env);
                GoldilocksField::poly_mul(&pa, &pb)
            }

            SymbolicExpression::Neg(a) => {
                let pa = a.to_polynomial_form(var_col, env);
                pa.iter().map(|c| -*c).collect()
            }

            SymbolicExpression::Pow(base, exp) => {
                let pb = base.to_polynomial_form(var_col, env);
                let mut result = vec![GoldilocksField::ONE];
                for _ in 0..*exp {
                    result = GoldilocksField::poly_mul(&result, &pb);
                }
                result
            }
        }
    }

    // ── Predicates ───────────────────────────────────────────────────────

    /// True if this node is a `Constant`.
    pub fn is_constant(&self) -> bool {
        matches!(self, SymbolicExpression::Constant(_))
    }

    /// If this node is a constant, return its value.
    pub fn constant_value(&self) -> Option<GoldilocksField> {
        match self {
            SymbolicExpression::Constant(c) => Some(*c),
            _ => None,
        }
    }

    /// Collect all `(col, row_offset)` pairs referenced in this expression.
    pub fn variables_used(&self) -> BTreeSet<(usize, i32)> {
        let mut out = BTreeSet::new();
        self.collect_variables(&mut out);
        out
    }

    fn collect_variables(&self, acc: &mut BTreeSet<(usize, i32)>) {
        match self {
            SymbolicExpression::Constant(_) => {}
            SymbolicExpression::Variable { col, row_offset } => {
                acc.insert((*col, *row_offset));
            }
            SymbolicExpression::CurrentRow(col) => {
                acc.insert((*col, 0));
            }
            SymbolicExpression::NextRow(col) => {
                acc.insert((*col, 1));
            }
            SymbolicExpression::Add(a, b)
            | SymbolicExpression::Mul(a, b)
            | SymbolicExpression::Sub(a, b) => {
                a.collect_variables(acc);
                b.collect_variables(acc);
            }
            SymbolicExpression::Neg(a) => a.collect_variables(acc),
            SymbolicExpression::Pow(base, _) => base.collect_variables(acc),
        }
    }

    /// Count the total number of AST nodes.
    pub fn node_count(&self) -> usize {
        match self {
            SymbolicExpression::Constant(_)
            | SymbolicExpression::Variable { .. }
            | SymbolicExpression::CurrentRow(_)
            | SymbolicExpression::NextRow(_) => 1,

            SymbolicExpression::Add(a, b)
            | SymbolicExpression::Mul(a, b)
            | SymbolicExpression::Sub(a, b) => 1 + a.node_count() + b.node_count(),

            SymbolicExpression::Neg(a) => 1 + a.node_count(),
            SymbolicExpression::Pow(base, _) => 1 + base.node_count(),
        }
    }

    /// Return true if the expression references the next row at all.
    pub fn uses_next_row(&self) -> bool {
        match self {
            SymbolicExpression::Constant(_) => false,
            SymbolicExpression::Variable { row_offset, .. } => *row_offset > 0,
            SymbolicExpression::CurrentRow(_) => false,
            SymbolicExpression::NextRow(_) => true,
            SymbolicExpression::Add(a, b)
            | SymbolicExpression::Mul(a, b)
            | SymbolicExpression::Sub(a, b) => a.uses_next_row() || b.uses_next_row(),
            SymbolicExpression::Neg(a) => a.uses_next_row(),
            SymbolicExpression::Pow(base, _) => base.uses_next_row(),
        }
    }

    /// Normalize the expression so that `CurrentRow(c)` → `Variable{c,0}`
    /// and `NextRow(c)` → `Variable{c,1}`.
    pub fn normalize(&self) -> Self {
        match self {
            SymbolicExpression::CurrentRow(col) => {
                SymbolicExpression::Variable { col: *col, row_offset: 0 }
            }
            SymbolicExpression::NextRow(col) => {
                SymbolicExpression::Variable { col: *col, row_offset: 1 }
            }
            SymbolicExpression::Constant(_) | SymbolicExpression::Variable { .. } => self.clone(),
            SymbolicExpression::Add(a, b) => {
                SymbolicExpression::Add(Box::new(a.normalize()), Box::new(b.normalize()))
            }
            SymbolicExpression::Mul(a, b) => {
                SymbolicExpression::Mul(Box::new(a.normalize()), Box::new(b.normalize()))
            }
            SymbolicExpression::Sub(a, b) => {
                SymbolicExpression::Sub(Box::new(a.normalize()), Box::new(b.normalize()))
            }
            SymbolicExpression::Neg(a) => SymbolicExpression::Neg(Box::new(a.normalize())),
            SymbolicExpression::Pow(base, exp) => {
                SymbolicExpression::Pow(Box::new(base.normalize()), *exp)
            }
        }
    }
}

