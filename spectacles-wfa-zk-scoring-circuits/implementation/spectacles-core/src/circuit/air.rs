// AIROptimizer
// ═══════════════════════════════════════════════════════════════════════════

/// Optimizer for AIR programs: merges, eliminates, reorders, and splits
/// constraints to produce more efficient programs.
pub struct AIROptimizer;

impl AIROptimizer {
    /// Run all optimization passes on the given program and return an optimized copy.
    pub fn optimize(program: &AIRProgram) -> AIRProgram {
        let mut constraints = program.constraints.clone();

        // 1. Eliminate redundant constraints
        constraints = Self::eliminate_redundant(&constraints);

        // 2. Merge compatible constraints
        constraints = Self::merge_compatible_constraints(&constraints);

        let mut optimized = AIRProgram::new(
            format!("{}_optimized", program.name),
            program.layout.clone(),
        );
        for c in constraints {
            optimized.add_constraint(c);
        }
        // Copy over boundary descriptors and periodic columns
        for bd in program.boundary_descriptors() {
            optimized.boundary_descriptors.push(bd.clone());
        }
        for pc in program.periodic_columns() {
            optimized.periodic_columns.push(pc.clone());
        }
        optimized
    }

    /// Merge pairs of constraints that share the same type and can be combined.
    /// Transition constraints with identical referenced columns are summed
    /// (random-linear-combination style with coefficient 1, simplifying).
    pub fn merge_compatible_constraints(
        constraints: &[AIRConstraint],
    ) -> Vec<AIRConstraint> {
        if constraints.len() < 2 {
            return constraints.to_vec();
        }

        let mut merged = Vec::new();
        let mut used = vec![false; constraints.len()];

        for i in 0..constraints.len() {
            if used[i] { continue; }

            let mut current = constraints[i].clone();

            for j in (i + 1)..constraints.len() {
                if used[j] { continue; }
                if constraints[j].constraint_type != current.constraint_type {
                    continue;
                }
                // Only merge transition constraints that reference the same columns
                if current.is_transition()
                    && current.referenced_columns() == constraints[j].referenced_columns()
                {
                    let combined_expr = SymbolicExpression::Mul(
                        Box::new(current.expression.clone()),
                        Box::new(constraints[j].expression.clone()),
                    );
                    current = AIRConstraint::new(
                        format!("{}+{}", current.name, constraints[j].name),
                        current.constraint_type,
                        combined_expr,
                        current.boundary_row,
                    );
                    current.period = constraints[i].period;
                    used[j] = true;
                }
            }

            merged.push(current);
        }

        merged
    }

    /// Remove constraints that are trivially redundant:
    /// - constraints whose expression simplifies to the constant 0
    /// - exact duplicate expressions
    pub fn eliminate_redundant(
        constraints: &[AIRConstraint],
    ) -> Vec<AIRConstraint> {
        let mut result = Vec::new();
        let mut seen_exprs: HashSet<String> = HashSet::new();

        for c in constraints {
            let simplified = c.expression.simplify_full();
            // Skip trivially zero constraints
            if simplified.is_constant() {
                if let Some(val) = simplified.constant_value() {
                    if val == GoldilocksField::ZERO {
                        continue;
                    }
                }
            }
            // Skip duplicate expressions
            let expr_key = format!("{:?}_{:?}", c.constraint_type, simplified);
            if seen_exprs.contains(&expr_key) {
                continue;
            }
            seen_exprs.insert(expr_key);

            let mut new_c = c.clone();
            new_c.expression = simplified;
            new_c.degree = new_c.expression.degree();
            result.push(new_c);
        }
        result
    }

    /// Reorder trace columns so that columns accessed together in constraints
    /// are adjacent, improving cache locality during evaluation.
    /// Returns a new AIRProgram with columns reordered.
    pub fn reorder_for_cache(
        trace: &AIRTrace,
        program: &AIRProgram,
    ) -> AIRProgram {
        // Build column co-occurrence counts
        let mut cooccurrence: HashMap<(usize, usize), usize> = HashMap::new();
        for c in &program.constraints {
            let cols: Vec<usize> = c.referenced_columns().into_iter().collect();
            for i in 0..cols.len() {
                for j in (i + 1)..cols.len() {
                    let key = (cols[i].min(cols[j]), cols[i].max(cols[j]));
                    *cooccurrence.entry(key).or_insert(0) += 1;
                }
            }
        }

        // Greedy ordering: start with the most-referenced column, then pick
        // the column with the highest co-occurrence with already-placed columns
        let num_cols = program.layout.num_columns;
        let mut col_refs = vec![0usize; num_cols];
        for c in &program.constraints {
            for &col in &c.referenced_columns() {
                if col < num_cols {
                    col_refs[col] += 1;
                }
            }
        }

        let mut order: Vec<usize> = Vec::with_capacity(num_cols);
        let mut remaining: HashSet<usize> = (0..num_cols).collect();

        // Start with the most-referenced column
        if let Some(&first) = remaining.iter().max_by_key(|&&c| col_refs[c]) {
            order.push(first);
            remaining.remove(&first);
        }

        while !remaining.is_empty() {
            let mut best_col = *remaining.iter().next().unwrap();
            let mut best_score = 0usize;
            for &candidate in &remaining {
                let score: usize = order.iter()
                    .map(|&placed| {
                        let key = (candidate.min(placed), candidate.max(placed));
                        cooccurrence.get(&key).copied().unwrap_or(0)
                    })
                    .sum();
                if score > best_score {
                    best_score = score;
                    best_col = candidate;
                }
            }
            order.push(best_col);
            remaining.remove(&best_col);
        }

        // Build column mapping: old_col -> new_col
        let mut mapping = vec![0usize; num_cols];
        for (new_idx, &old_idx) in order.iter().enumerate() {
            mapping[old_idx] = new_idx;
        }

        // Remap constraints
        let _ = trace; // trace provided for future extensions
        let mut new_program = AIRProgram::new(
            format!("{}_reordered", program.name),
            program.layout.clone(),
        );
        for c in &program.constraints {
            let remapped_expr = Self::remap_columns(&c.expression, &mapping);
            let mut new_c = AIRConstraint::new(
                c.name.clone(),
                c.constraint_type,
                remapped_expr,
                c.boundary_row,
            );
            new_c.period = c.period;
            new_program.add_constraint(new_c);
        }
        for bd in program.boundary_descriptors() {
            let new_col = mapping[bd.column];
            new_program.boundary_descriptors.push(
                BoundaryDescriptor::new(new_col, bd.row, bd.value),
            );
        }
        for pc in program.periodic_columns() {
            new_program.periodic_columns.push(pc.clone());
        }
        new_program
    }

    /// Remap column indices in a symbolic expression.
    fn remap_columns(
        expr: &SymbolicExpression,
        mapping: &[usize],
    ) -> SymbolicExpression {
        match expr {
            SymbolicExpression::Constant(_) => expr.clone(),
            SymbolicExpression::Variable { col, row_offset } => {
                SymbolicExpression::Variable {
                    col: mapping[*col],
                    row_offset: *row_offset,
                }
            }
            SymbolicExpression::CurrentRow(col) => {
                SymbolicExpression::CurrentRow(mapping[*col])
            }
            SymbolicExpression::NextRow(col) => {
                SymbolicExpression::NextRow(mapping[*col])
            }
            SymbolicExpression::Add(a, b) => SymbolicExpression::Add(
                Box::new(Self::remap_columns(a, mapping)),
                Box::new(Self::remap_columns(b, mapping)),
            ),
            SymbolicExpression::Mul(a, b) => SymbolicExpression::Mul(
                Box::new(Self::remap_columns(a, mapping)),
                Box::new(Self::remap_columns(b, mapping)),
            ),
            SymbolicExpression::Sub(a, b) => SymbolicExpression::Sub(
                Box::new(Self::remap_columns(a, mapping)),
                Box::new(Self::remap_columns(b, mapping)),
            ),
            SymbolicExpression::Neg(a) => SymbolicExpression::Neg(
                Box::new(Self::remap_columns(a, mapping)),
            ),
            SymbolicExpression::Pow(base, exp) => SymbolicExpression::Pow(
                Box::new(Self::remap_columns(base, mapping)),
                *exp,
            ),
        }
    }

    /// Split a high-degree constraint into multiple lower-degree constraints
    /// by introducing auxiliary columns for intermediate products.
    ///
    /// For example, a degree-4 constraint `a*b*c*d = 0` with max_degree=2 becomes:
    ///   aux1 = a*b, aux1*c*d = 0 (further split if still too high).
    pub fn split_high_degree(
        constraint: &AIRConstraint,
        max_degree: usize,
        next_aux: &mut usize,
    ) -> Vec<AIRConstraint> {
        if constraint.degree <= max_degree {
            return vec![constraint.clone()];
        }

        let mut results = Vec::new();
        Self::split_recursive(
            &constraint.name,
            &constraint.expression,
            constraint.constraint_type,
            constraint.boundary_row,
            constraint.period,
            max_degree,
            next_aux,
            &mut results,
        );
        results
    }

    fn split_recursive(
        name: &str,
        expr: &SymbolicExpression,
        ctype: ConstraintType,
        boundary_row: Option<usize>,
        period: Option<usize>,
        max_degree: usize,
        next_aux: &mut usize,
        results: &mut Vec<AIRConstraint>,
    ) {
        let deg = expr.degree();
        if deg <= max_degree {
            let mut c = AIRConstraint::new(name.to_string(), ctype, expr.clone(), boundary_row);
            c.period = period;
            results.push(c);
            return;
        }

        // For Mul nodes, split into two halves using an auxiliary variable
        if let SymbolicExpression::Mul(a, b) = expr {
            let aux_col = *next_aux;
            *next_aux += 1;

            // aux_col = a (definition constraint: aux - a = 0)
            let def_expr = SymbolicExpression::Sub(
                Box::new(SymbolicExpression::CurrentRow(aux_col)),
                a.clone(),
            );
            let mut def_c = AIRConstraint::new(
                format!("{}_split_def_{}", name, aux_col),
                ctype,
                def_expr,
                boundary_row,
            );
            def_c.period = period;
            results.push(def_c);

            // Now the original becomes aux_col * b
            let reduced = SymbolicExpression::Mul(
                Box::new(SymbolicExpression::CurrentRow(aux_col)),
                b.clone(),
            );
            Self::split_recursive(
                &format!("{}_split_{}", name, aux_col),
                &reduced,
                ctype,
                boundary_row,
                period,
                max_degree,
                next_aux,
                results,
            );
        } else {
            // Can't split non-Mul high-degree nodes further; emit as-is
            let mut c = AIRConstraint::new(name.to_string(), ctype, expr.clone(), boundary_row);
            c.period = period;
            results.push(c);
        }
    }

    /// Estimate the verification cost of an AIR program (heuristic).
    /// Based on constraint count weighted by degree.
    pub fn estimated_verification_cost(program: &AIRProgram) -> usize {
        program.constraints.iter()
            .map(|c| c.degree + 1)
            .sum()
    }
}

