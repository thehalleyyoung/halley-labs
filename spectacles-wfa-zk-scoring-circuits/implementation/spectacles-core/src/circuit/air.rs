/// A complete AIR: a set of constraints together with a trace layout.
#[derive(Debug, Clone)]
pub struct AIRProgram {
    /// All constraints.
    pub constraints: Vec<AIRConstraint>,
    /// Trace schema.
    pub layout: TraceLayout,
    /// Cached maximum constraint degree.
    pub max_constraint_degree: usize,
    /// Name of this program.
    pub name: String,
    /// Boundary descriptors (convenience; also reflected in `constraints`).
    boundary_descriptors: Vec<BoundaryDescriptor>,
    /// Periodic columns referenced by periodic constraints.
    periodic_columns: Vec<PeriodicColumn>,
}

impl AIRProgram {
    /// Create a new, empty AIR program.
    pub fn new(name: impl Into<String>, layout: TraceLayout) -> Self {
        Self {
            constraints: Vec::new(),
            layout,
            max_constraint_degree: 0,
            name: name.into(),
            boundary_descriptors: Vec::new(),
            periodic_columns: Vec::new(),
        }
    }

    /// Add a generic constraint.
    pub fn add_constraint(&mut self, constraint: AIRConstraint) {
        let deg = constraint.degree;
        if deg > self.max_constraint_degree {
            self.max_constraint_degree = deg;
        }
        self.constraints.push(constraint);
    }

    /// Convenience: add a boundary constraint from a descriptor.
    pub fn add_boundary_constraint(
        &mut self,
        column: usize,
        row: usize,
        value: GoldilocksField,
    ) {
        let desc = BoundaryDescriptor::new(column, row, value);
        let constraint = desc.to_constraint();
        self.add_constraint(constraint);
        self.boundary_descriptors.push(desc);
    }

    /// Convenience: add a transition constraint (expression that must be
    /// zero for every consecutive row pair).
    pub fn add_transition_constraint(
        &mut self,
        name: impl Into<String>,
        expression: SymbolicExpression,
    ) {
        let c = AIRConstraint::new(name, ConstraintType::Transition, expression, None);
        self.add_constraint(c);
    }

    /// Convenience: add a periodic constraint.
    pub fn add_periodic_constraint(
        &mut self,
        name: impl Into<String>,
        expression: SymbolicExpression,
        period: usize,
    ) {
        let c = AIRConstraint::new_periodic(name, expression, period);
        self.add_constraint(c);
    }

    /// Register a periodic column for use by constraints.
    pub fn add_periodic_column(&mut self, pc: PeriodicColumn) -> usize {
        let idx = self.periodic_columns.len();
        self.periodic_columns.push(pc);
        idx
    }

    /// Verify that every constraint evaluates to zero on the supplied trace.
    /// Returns a list of (constraint_index, failing_row) pairs.
    pub fn verify_trace(&self, trace: &AIRTrace) -> Vec<(usize, usize)> {
        let mut failures = Vec::new();

        for (ci, constraint) in self.constraints.iter().enumerate() {
            match constraint.constraint_type {
                ConstraintType::Boundary => {
                    if let Some(row) = constraint.boundary_row {
                        if row < trace.num_rows {
                            let tr = trace.get_row(row);
                            if !constraint.verify_boundary(tr) {
                                failures.push((ci, row));
                            }
                        }
                    }
                }
                ConstraintType::Transition => {
                    for r in 0..trace.num_rows.saturating_sub(1) {
                        let (cur, nxt) = trace.window_at(r);
                        if !constraint.verify_at_row(cur, nxt) {
                            failures.push((ci, r));
                        }
                    }
                }
                ConstraintType::Periodic => {
                    let period = constraint.period.unwrap_or(1);
                    for r in (0..trace.num_rows.saturating_sub(1)).step_by(period.max(1)) {
                        let (cur, nxt) = trace.window_at(r);
                        if !constraint.verify_at_row(cur, nxt) {
                            failures.push((ci, r));
                        }
                    }
                }
                ConstraintType::Composition => {
                    for r in 0..trace.num_rows.saturating_sub(1) {
                        let (cur, nxt) = trace.window_at(r);
                        if !constraint.verify_at_row(cur, nxt) {
                            failures.push((ci, r));
                        }
                    }
                }
            }
        }

        failures
    }

    /// Compose all constraints into a single polynomial evaluation using
    /// random challenges `alphas`.  The composed evaluation at row `row` is:
    ///
    ///   C(row) = Σ_i  α_i · c_i(row, row+1)
    ///
    /// Returns the vector of composed evaluations for every row.
    pub fn composition_polynomial(
        &self,
        trace: &AIRTrace,
        alphas: &[GoldilocksField],
    ) -> Vec<GoldilocksField> {
        let n = trace.num_rows;
        let mut result = vec![GoldilocksField::ZERO; n];

        for (ci, constraint) in self.constraints.iter().enumerate() {
            let alpha = if ci < alphas.len() {
                alphas[ci]
            } else {
                GoldilocksField::ONE
            };

            match constraint.constraint_type {
                ConstraintType::Boundary => {
                    if let Some(row) = constraint.boundary_row {
                        if row < n {
                            let tr = trace.get_row(row);
                            let val = constraint.expression.evaluate_single_row(tr);
                            result[row] = result[row] + alpha * val;
                        }
                    }
                }
                ConstraintType::Transition | ConstraintType::Composition => {
                    for r in 0..n.saturating_sub(1) {
                        let (cur, nxt) = trace.window_at(r);
                        let val = constraint.expression.evaluate(cur, nxt);
                        result[r] = result[r] + alpha * val;
                    }
                }
                ConstraintType::Periodic => {
                    let period = constraint.period.unwrap_or(1).max(1);
                    for r in (0..n.saturating_sub(1)).step_by(period) {
                        let (cur, nxt) = trace.window_at(r);
                        let val = constraint.expression.evaluate(cur, nxt);
                        result[r] = result[r] + alpha * val;
                    }
                }
            }
        }

        result
    }

    /// Number of constraints.
    pub fn constraint_count(&self) -> usize {
        self.constraints.len()
    }

    /// Return only the boundary constraints.
    pub fn boundary_constraints(&self) -> Vec<&AIRConstraint> {
        self.constraints
            .iter()
            .filter(|c| c.is_boundary())
            .collect()
    }

    /// Return only the transition constraints.
    pub fn transition_constraints(&self) -> Vec<&AIRConstraint> {
        self.constraints
            .iter()
            .filter(|c| c.is_transition())
            .collect()
    }

    /// Return only the periodic constraints.
    pub fn periodic_constraints(&self) -> Vec<&AIRConstraint> {
        self.constraints
            .iter()
            .filter(|c| c.is_periodic())
            .collect()
    }

    /// Maximum constraint degree across all constraints.
    pub fn max_degree(&self) -> usize {
        self.max_constraint_degree
    }

    /// Validate the program for internal consistency.
    pub fn validate_program(&self) -> Result<(), Vec<String>> {
        let mut errors = Vec::new();

        if let Err(e) = self.layout.validate() {
            errors.push(format!("Layout error: {}", e));
        }

        for (i, constraint) in self.constraints.iter().enumerate() {
            let vars = constraint.expression.variables_used();
            for (col, _) in &vars {
                if *col >= self.layout.num_columns {
                    errors.push(format!(
                        "Constraint '{}' (#{}) references column {} but layout has only {} columns",
                        constraint.name, i, col, self.layout.num_columns
                    ));
                }
            }

            if constraint.is_boundary() && constraint.boundary_row.is_none() {
                errors.push(format!(
                    "Boundary constraint '{}' (#{}) has no boundary_row",
                    constraint.name, i
                ));
            }
        }

        for desc in &self.boundary_descriptors {
            if desc.column >= self.layout.num_columns {
                errors.push(format!(
                    "Boundary descriptor references column {} but layout has {} columns",
                    desc.column, self.layout.num_columns
                ));
            }
        }

        if errors.is_empty() {
            Ok(())
        } else {
            Err(errors)
        }
    }

    /// Build an AIR program from a WFA specification.
    ///
    /// `num_states` – number of WFA states
    /// `alphabet_size` – number of input symbols
    /// `initial_state` – the starting state index
    /// `initial_weight` – the starting accumulator value
    ///
    /// The generated constraints enforce:
    ///   1. Boundary: state_id[0] = initial_state
    ///   2. Boundary: accumulator[0] = initial_weight
    ///   3. Transition: accumulator' = accumulator + transition_weight
    ///   4. Transition: step_count' = step_count + 1
    pub fn from_wfa_spec(
        num_states: usize,
        _alphabet_size: usize,
        initial_state: u64,
        initial_weight: u64,
    ) -> Self {
        let layout = TraceLayout::from_wfa_state_count(num_states);
        let mut program = AIRProgram::new("wfa_air", layout);

        let state_id_col = 0;
        let accumulator_col = program.layout.column_index_by_name("accumulator").unwrap();
        let transition_weight_col = program
            .layout
            .column_index_by_name("transition_weight")
            .unwrap();
        let step_count_col = program.layout.column_index_by_name("step_count").unwrap();

        program.add_boundary_constraint(
            state_id_col,
            0,
            GoldilocksField::new(initial_state),
        );
        program.add_boundary_constraint(
            accumulator_col,
            0,
            GoldilocksField::new(initial_weight),
        );
        program.add_boundary_constraint(step_count_col, 0, GoldilocksField::ZERO);

        let acc_transition = SymbolicExpression::NextRow(accumulator_col)
            - SymbolicExpression::CurrentRow(accumulator_col)
            - SymbolicExpression::CurrentRow(transition_weight_col);
        program.add_transition_constraint("accumulator_update", acc_transition);

        let step_transition = SymbolicExpression::NextRow(step_count_col)
            - SymbolicExpression::CurrentRow(step_count_col)
            - SymbolicExpression::one();
        program.add_transition_constraint("step_count_increment", step_transition);

        program
    }

    /// Return the boundary descriptors.
    pub fn boundary_descriptors(&self) -> &[BoundaryDescriptor] {
        &self.boundary_descriptors
    }

    /// Return the periodic columns.
    pub fn periodic_columns(&self) -> &[PeriodicColumn] {
        &self.periodic_columns
    }

    /// Get a constraint by name.
    pub fn get_constraint_by_name(&self, name: &str) -> Option<&AIRConstraint> {
        self.constraints.iter().find(|c| c.name == name)
    }

    /// Compute the "zerofier" polynomial for transition constraints:
    /// Z(x) = x^n - 1 where n = trace length.
    pub fn transition_zerofier_coeffs(&self, trace_len: usize) -> Vec<GoldilocksField> {
        let mut coeffs = vec![GoldilocksField::ZERO; trace_len + 1];
        coeffs[0] = -GoldilocksField::ONE;
        coeffs[trace_len] = GoldilocksField::ONE;
        coeffs
    }

    /// Compute the boundary zerofier for a specific row:
    /// Z_b(x) = x - ω^row   where ω is the n-th root of unity.
    pub fn boundary_zerofier_coeffs(
        &self,
        row: usize,
        trace_len: usize,
    ) -> Vec<GoldilocksField> {
        let omega = GoldilocksField::root_of_unity(trace_len);
        let omega_row = omega.pow(row as u64);
        vec![-omega_row, GoldilocksField::ONE]
    }

    /// Summary statistics.
    pub fn summary(&self) -> String {
        let bc = self.boundary_constraints().len();
        let tc = self.transition_constraints().len();
        let pc = self.periodic_constraints().len();
        format!(
            "AIRProgram '{}': {} columns, {} constraints ({}B + {}T + {}P), max_degree={}",
            self.name,
            self.layout.num_columns,
            self.constraints.len(),
            bc,
            tc,
            pc,
            self.max_constraint_degree,
        )
    }
}

impl fmt::Display for AIRProgram {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "{}", self.summary())
    }
}

