/// Represents values over a repeating domain of a fixed period.
/// The values repeat cyclically: value_at(i) = values[i % period].
#[derive(Debug, Clone)]
pub struct PeriodicPolynomial {
    values: Vec<GoldilocksField>,
}

impl PeriodicPolynomial {
    /// Create a new periodic polynomial with the given repeating values.
    pub fn new(values: Vec<GoldilocksField>) -> Self {
        assert!(!values.is_empty(), "PeriodicPolynomial must have at least one value");
        Self { values }
    }

    /// Evaluate at a given row index (cyclically).
    pub fn evaluate_at(&self, row: usize) -> GoldilocksField {
        self.values[row % self.values.len()]
    }

    /// Return the period (length of the repeating pattern).
    pub fn period(&self) -> usize {
        self.values.len()
    }

    /// Convert to polynomial coefficients via inverse-DFT-like reconstruction.
    /// For a period-N polynomial p with values v_0..v_{N-1},
    /// we use Lagrange interpolation over roots of unity.
    pub fn to_polynomial(&self) -> Vec<GoldilocksField> {
        let n = self.values.len();
        if n == 1 {
            return vec![self.values[0]];
        }
        // Simple DFT-based approach: coefficients via inverse NTT
        // c_k = (1/N) * sum_{j=0}^{N-1} v_j * omega^{-jk}
        let n_field = GoldilocksField::new(n as u64);
        let n_inv = n_field.inv_or_panic();
        let omega = GoldilocksField::root_of_unity(n);
        let omega_inv = omega.inv_or_panic();

        let mut coeffs = Vec::with_capacity(n);
        for k in 0..n {
            let mut sum = GoldilocksField::ZERO;
            for j in 0..n {
                let exp = ((j * k) % n) as u64;
                sum = sum + self.values[j] * omega_inv.pow(exp);
            }
            coeffs.push(sum * n_inv);
        }
        coeffs
    }

    /// Compose this periodic polynomial with another by element-wise multiplication
    /// over the LCM of their periods.
    pub fn compose_with(&self, other: &PeriodicPolynomial) -> PeriodicPolynomial {
        let p1 = self.period();
        let p2 = other.period();
        let lcm = lcm_usize(p1, p2);
        let mut values = Vec::with_capacity(lcm);
        for i in 0..lcm {
            values.push(self.evaluate_at(i) * other.evaluate_at(i));
        }
        PeriodicPolynomial { values }
    }
}

/// Compute the least common multiple of two usize values.
fn lcm_usize(a: usize, b: usize) -> usize {
    if a == 0 || b == 0 { return 0; }
    a / gcd_usize(a, b) * b
}

/// Compute the greatest common divisor of two usize values.
fn gcd_usize(mut a: usize, mut b: usize) -> usize {
    while b != 0 {
        let t = b;
        b = a % b;
        a = t;
    }
    a
}

