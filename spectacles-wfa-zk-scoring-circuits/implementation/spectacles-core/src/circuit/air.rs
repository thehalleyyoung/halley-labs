// AIRTrace
// ═══════════════════════════════════════════════════════════════════════════

/// A concrete execution trace: a 2-D table of field elements.
#[derive(Debug, Clone)]
pub struct AIRTrace {
    /// Row-major data: data[row][col].
    pub data: Vec<Vec<GoldilocksField>>,
    /// Schema of the trace.
    pub layout: TraceLayout,
    /// Number of rows.
    pub num_rows: usize,
    /// Number of columns.
    pub num_cols: usize,
}

impl AIRTrace {
    /// Create a zero-initialised trace with the given dimensions and layout.
    pub fn new(num_rows: usize, layout: TraceLayout) -> Self {
        let num_cols = layout.num_columns;
        let data = vec![vec![GoldilocksField::ZERO; num_cols]; num_rows];
        Self {
            data,
            layout,
            num_rows,
            num_cols,
        }
    }

    /// Create a trace with the given dimensions but no layout metadata.
    pub fn new_raw(num_rows: usize, num_cols: usize) -> Self {
        let mut layout = TraceLayout::new();
        for i in 0..num_cols {
            layout.add_column(format!("col_{}", i), ColumnType::State);
        }
        let data = vec![vec![GoldilocksField::ZERO; num_cols]; num_rows];
        Self {
            data,
            layout,
            num_rows,
            num_cols,
        }
    }

    /// Read a single cell.
    pub fn get(&self, row: usize, col: usize) -> GoldilocksField {
        self.data[row][col]
    }

    /// Write a single cell.
    pub fn set(&mut self, row: usize, col: usize, val: GoldilocksField) {
        self.data[row][col] = val;
    }

    /// Return a full row as a slice.
    pub fn get_row(&self, row: usize) -> &[GoldilocksField] {
        &self.data[row]
    }

    /// Return an owned copy of one column.
    pub fn get_column(&self, col: usize) -> Vec<GoldilocksField> {
        self.data.iter().map(|row| row[col]).collect()
    }

    /// Pad the trace to the next power of two by repeating the last row.
    pub fn pad_to_power_of_two(&mut self) {
        let target = self.num_rows.next_power_of_two();
        if target == self.num_rows {
            return;
        }
        let last_row = self.data.last().cloned().unwrap_or_else(|| {
            vec![GoldilocksField::ZERO; self.num_cols]
        });
        while self.data.len() < target {
            self.data.push(last_row.clone());
        }
        self.num_rows = target;
    }

    /// Validate that `data` dimensions agree with `num_rows` / `num_cols`.
    pub fn validate_dimensions(&self) -> Result<(), String> {
        if self.data.len() != self.num_rows {
            return Err(format!(
                "Row count mismatch: data has {} rows but num_rows={}",
                self.data.len(),
                self.num_rows
            ));
        }
        for (i, row) in self.data.iter().enumerate() {
            if row.len() != self.num_cols {
                return Err(format!(
                    "Row {} has {} cols but expected {}",
                    i,
                    row.len(),
                    self.num_cols
                ));
            }
        }
        if self.num_cols != self.layout.num_columns {
            return Err(format!(
                "num_cols={} but layout has {} columns",
                self.num_cols,
                self.layout.num_columns
            ));
        }
        Ok(())
    }

    /// Build a trace from a list of rows.
    pub fn from_rows(rows: Vec<Vec<GoldilocksField>>, layout: TraceLayout) -> Self {
        let num_rows = rows.len();
        let num_cols = if num_rows > 0 { rows[0].len() } else { layout.num_columns };
        Self {
            data: rows,
            layout,
            num_rows,
            num_cols,
        }
    }

    /// Build a trace from column-major data.
    pub fn from_columns(columns: Vec<Vec<GoldilocksField>>, layout: TraceLayout) -> Self {
        if columns.is_empty() {
            return Self::new(0, layout);
        }
        let num_cols = columns.len();
        let num_rows = columns[0].len();
        let mut data = vec![vec![GoldilocksField::ZERO; num_cols]; num_rows];
        for (c, col_data) in columns.iter().enumerate() {
            for (r, &val) in col_data.iter().enumerate() {
                data[r][c] = val;
            }
        }
        Self {
            data,
            layout,
            num_rows,
            num_cols,
        }
    }

    /// Transpose the trace (swap rows ↔ columns).  Layout column names are
    /// replaced with generic names.
    pub fn transpose(&self) -> Self {
        let mut layout = TraceLayout::new();
        for i in 0..self.num_rows {
            layout.add_column(format!("row_{}", i), ColumnType::State);
        }
        let mut data = vec![vec![GoldilocksField::ZERO; self.num_rows]; self.num_cols];
        for r in 0..self.num_rows {
            for c in 0..self.num_cols {
                data[c][r] = self.data[r][c];
            }
        }
        Self {
            data,
            layout,
            num_rows: self.num_cols,
            num_cols: self.num_rows,
        }
    }

    /// Return the (current_row, next_row) window at position `row`.
    /// If `row` is the last row, `next_row` wraps to row 0.
    pub fn window_at(&self, row: usize) -> (&[GoldilocksField], &[GoldilocksField]) {
        let next = if row + 1 < self.num_rows { row + 1 } else { 0 };
        (&self.data[row], &self.data[next])
    }

    /// Extract a sub-trace containing only the specified columns (by index).
    pub fn sub_trace(&self, columns: &[usize]) -> Self {
        let mut layout = TraceLayout::new();
        for &c in columns {
            let original = &self.layout.columns[c];
            layout.add_column(original.name.clone(), original.column_type);
        }
        let data: Vec<Vec<GoldilocksField>> = self
            .data
            .iter()
            .map(|row| columns.iter().map(|&c| row[c]).collect())
            .collect();
        let num_cols = columns.len();
        Self {
            data,
            layout,
            num_rows: self.num_rows,
            num_cols,
        }
    }

    /// Low-degree extension: repeat each row `factor` times by inserting
    /// interpolated rows.  The simplest form just repeats; a proper LDE
    /// would evaluate the column polynomials on a larger domain.
    ///
    /// Here we perform per-column polynomial interpolation over roots of
    /// unity, then evaluate on a `factor`-times-larger domain.
    pub fn extend_trace(&self, factor: usize) -> Self {
        if factor <= 1 || self.num_rows == 0 {
            return self.clone();
        }
        let n = self.num_rows;
        let extended_len = n * factor;

        // We need n to be a power of two for root-of-unity interpolation.
        if !n.is_power_of_two() {
            let mut extended_data = Vec::with_capacity(extended_len);
            for row in &self.data {
                for _ in 0..factor {
                    extended_data.push(row.clone());
                }
            }
            return Self {
                data: extended_data,
                layout: self.layout.clone(),
                num_rows: extended_len,
                num_cols: self.num_cols,
            };
        }

        let omega = GoldilocksField::root_of_unity(n);
        let omega_ext = GoldilocksField::root_of_unity(extended_len);
        let shift = GoldilocksField::new(7);

        let mut extended_data = vec![vec![GoldilocksField::ZERO; self.num_cols]; extended_len];

        for c in 0..self.num_cols {
            let col_vals: Vec<GoldilocksField> = (0..n).map(|r| self.data[r][c]).collect();
            let xs: Vec<GoldilocksField> = (0..n)
                .map(|i| omega.pow(i as u64))
                .collect();
            let coeffs = GoldilocksField::lagrange_interpolation(&xs, &col_vals);

            for j in 0..extended_len {
                let x = shift * omega_ext.pow(j as u64);
                extended_data[j][c] = GoldilocksField::eval_poly(&coeffs, x);
            }
        }

        Self {
            data: extended_data,
            layout: self.layout.clone(),
            num_rows: extended_len,
            num_cols: self.num_cols,
        }
    }

    /// Set an entire row from a slice.
    pub fn set_row(&mut self, row: usize, values: &[GoldilocksField]) {
        let len = values.len().min(self.num_cols);
        self.data[row][..len].copy_from_slice(&values[..len]);
    }

    /// Set an entire column from a slice.
    pub fn set_column(&mut self, col: usize, values: &[GoldilocksField]) {
        let len = values.len().min(self.num_rows);
        for i in 0..len {
            self.data[i][col] = values[i];
        }
    }

    /// Append a row to the trace.
    pub fn push_row(&mut self, row: Vec<GoldilocksField>) {
        assert_eq!(row.len(), self.num_cols, "row width mismatch");
        self.data.push(row);
        self.num_rows += 1;
    }

    /// Return true if all cells are zero.
    pub fn is_zero(&self) -> bool {
        self.data.iter().all(|row| row.iter().all(|v| v.is_zero()))
    }

    /// Compute a column-wise hash (sum of all elements in each column,
    /// weighted by row index).  Useful as a quick fingerprint.
    pub fn column_fingerprints(&self) -> Vec<GoldilocksField> {
        let mut fps = vec![GoldilocksField::ZERO; self.num_cols];
        for (r, row) in self.data.iter().enumerate() {
            let weight = GoldilocksField::new(r as u64 + 1);
            for (c, &val) in row.iter().enumerate() {
                fps[c] = fps[c] + val * weight;
            }
        }
        fps
    }

    /// Check that two traces have identical dimensions and data.
    pub fn equals(&self, other: &AIRTrace) -> bool {
        if self.num_rows != other.num_rows || self.num_cols != other.num_cols {
            return false;
        }
        for r in 0..self.num_rows {
            for c in 0..self.num_cols {
                if self.data[r][c] != other.data[r][c] {
                    return false;
                }
            }
        }
        true
    }
}

impl fmt::Display for AIRTrace {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        writeln!(f, "AIRTrace({}×{})", self.num_rows, self.num_cols)?;
        let display_rows = self.num_rows.min(8);
        for r in 0..display_rows {
            write!(f, "  row {:>3}: [", r)?;
            for c in 0..self.num_cols {
                if c > 0 {
                    write!(f, ", ")?;
                }
                write!(f, "{}", self.data[r][c])?;
            }
            writeln!(f, "]")?;
        }
        if self.num_rows > display_rows {
            writeln!(f, "  ... ({} more rows)", self.num_rows - display_rows)?;
        }
        Ok(())
    }
}

// ═══════════════════════════════════════════════════════════════════════════
// BoundaryDescriptor
// ═══════════════════════════════════════════════════════════════════════════

/// Describes a point constraint: trace[row][column] must equal `value`.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct BoundaryDescriptor {
    /// Column index.
    pub column: usize,
    /// Row index.
    pub row: usize,
    /// Required value.
    pub value: GoldilocksField,
}

impl BoundaryDescriptor {
    pub fn new(column: usize, row: usize, value: GoldilocksField) -> Self {
        Self { column, row, value }
    }

    /// Convert this descriptor into an AIRConstraint.
    /// The expression is: CurrentRow(column) - value = 0.
    pub fn to_constraint(&self) -> AIRConstraint {
        let expr = SymbolicExpression::CurrentRow(self.column)
            - SymbolicExpression::Constant(self.value);
        AIRConstraint::new(
            format!("boundary_c{}_r{}", self.column, self.row),
            ConstraintType::Boundary,
            expr,
            Some(self.row),
        )
    }

    /// Check whether the trace satisfies this boundary.
    pub fn check(&self, trace: &AIRTrace) -> bool {
        if self.row >= trace.num_rows || self.column >= trace.num_cols {
            return false;
        }
        trace.get(self.row, self.column) == self.value
    }
}

impl fmt::Display for BoundaryDescriptor {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(
            f,
            "trace[{}][{}] = {}",
            self.row, self.column, self.value
        )
    }
}

// ═══════════════════════════════════════════════════════════════════════════
// PeriodicColumn
// ═══════════════════════════════════════════════════════════════════════════

/// A repeating sequence of field values used in periodic constraints.
#[derive(Debug, Clone)]
pub struct PeriodicColumn {
    /// The repeating values; length is the period.
    pub values: Vec<GoldilocksField>,
    /// The period (== values.len()).
    pub period: usize,
}

impl PeriodicColumn {
    /// Create a new periodic column.  `values` must be non-empty.
    pub fn new(values: Vec<GoldilocksField>) -> Self {
        let period = values.len();
        assert!(period > 0, "periodic column must have at least one value");
        Self { values, period }
    }

    /// Create a periodic column that alternates between two values.
    pub fn alternating(a: GoldilocksField, b: GoldilocksField) -> Self {
        Self::new(vec![a, b])
    }

    /// Create a periodic column that is 1 every `period` steps and 0
    /// otherwise (a selector).
    pub fn selector(period: usize) -> Self {
        let mut vals = vec![GoldilocksField::ZERO; period];
        vals[0] = GoldilocksField::ONE;
        Self::new(vals)
    }

    /// Return the value at row index `row` (wrapping).
    pub fn value_at(&self, row: usize) -> GoldilocksField {
        self.values[row % self.period]
    }

    /// Interpolate the periodic values into a polynomial over roots of
    /// unity of order `period`.  Returns the coefficient vector.
    pub fn to_polynomial(&self) -> Vec<GoldilocksField> {
        let n = self.period;
        if n == 0 {
            return vec![];
        }
        if n == 1 {
            return vec![self.values[0]];
        }

        if n.is_power_of_two() {
            let omega = GoldilocksField::root_of_unity(n);
            let xs: Vec<GoldilocksField> = (0..n)
                .map(|i| omega.pow(i as u64))
                .collect();
            GoldilocksField::lagrange_interpolation(&xs, &self.values)
        } else {
            let xs: Vec<GoldilocksField> = (1..=n)
                .map(|i| GoldilocksField::new(i as u64))
                .collect();
            GoldilocksField::lagrange_interpolation(&xs, &self.values)
        }
    }

    /// Evaluate the periodic-column polynomial at a field point `x`.
    pub fn evaluate_at(&self, x: GoldilocksField) -> GoldilocksField {
        let poly = self.to_polynomial();
        GoldilocksField::eval_poly(&poly, x)
    }

    /// Extend the periodic column to fill `total_rows` (just wrapping).
    pub fn expand(&self, total_rows: usize) -> Vec<GoldilocksField> {
        (0..total_rows).map(|i| self.value_at(i)).collect()
    }
}

impl fmt::Display for PeriodicColumn {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "PeriodicColumn(period={}, values={:?})", self.period, self.values)
    }
}

