// ═══════════════════════════════════════════════════════════════════════════

/// Compute the degree adjustments needed for each constraint so that all
/// have the same degree after multiplication by the adjustment.
pub fn compute_degree_adjustments(
    constraints: &[AIRConstraint],
    trace_len: usize,
) -> Vec<usize> {
    let max_comp_deg: usize = constraints
        .iter()
        .map(|c| c.degree * trace_len)
        .max()
        .unwrap_or(0);

    constraints
        .iter()
        .map(|c| max_comp_deg.saturating_sub(c.degree * trace_len))
        .collect()
}

/// Compute the constraint degree bound for the composition polynomial.
pub fn composition_degree_bound(
    constraints: &[AIRConstraint],
    trace_len: usize,
) -> usize {
    constraints
        .iter()
        .map(|c| c.degree * trace_len)
        .max()
        .unwrap_or(0)
}

