// ═══════════════════════════════════════════════════════════════════════════

/// A graph representation where nodes are constraints and edges connect
/// constraints that share at least one column.
#[derive(Debug, Clone)]
pub struct ConstraintGraph {
    /// Number of nodes (constraints).
    pub num_nodes: usize,
    /// Adjacency list: for each constraint index, the set of constraint indices
    /// it shares columns with.
    pub adjacency: Vec<HashSet<usize>>,
    /// For each constraint, the set of columns it references.
    pub node_columns: Vec<BTreeSet<usize>>,
}

impl ConstraintGraph {
    /// Find the connected components of the constraint graph.
    /// Returns a vector of components, each being a vector of constraint indices.
    pub fn connected_components(&self) -> Vec<Vec<usize>> {
        let mut visited = vec![false; self.num_nodes];
        let mut components = Vec::new();
        for start in 0..self.num_nodes {
            if visited[start] {
                continue;
            }
            let mut component = Vec::new();
            let mut stack = vec![start];
            while let Some(node) = stack.pop() {
                if visited[node] {
                    continue;
                }
                visited[node] = true;
                component.push(node);
                for &neighbor in &self.adjacency[node] {
                    if !visited[neighbor] {
                        stack.push(neighbor);
                    }
                }
            }
            component.sort_unstable();
            components.push(component);
        }
        components
    }

    /// Check whether the constraint graph is fully connected.
    pub fn is_connected(&self) -> bool {
        if self.num_nodes == 0 {
            return true;
        }
        let components = self.connected_components();
        components.len() == 1
    }

    /// Find independent sets using a greedy algorithm.
    /// Returns sets of constraint indices that share no columns.
    pub fn independent_sets(&self) -> Vec<Vec<usize>> {
        let mut remaining: HashSet<usize> = (0..self.num_nodes).collect();
        let mut sets = Vec::new();
        while !remaining.is_empty() {
            let mut current_set = Vec::new();
            let mut used_columns: BTreeSet<usize> = BTreeSet::new();
            let mut to_remove = Vec::new();
            for &node in &remaining {
                let node_cols = &self.node_columns[node];
                if node_cols.iter().all(|c| !used_columns.contains(c)) {
                    current_set.push(node);
                    used_columns.extend(node_cols.iter());
                    to_remove.push(node);
                }
            }
            for node in &to_remove {
                remaining.remove(node);
            }
            if current_set.is_empty() {
                // Safety: pick one remaining node to avoid infinite loop
                if let Some(&node) = remaining.iter().next() {
                    current_set.push(node);
                    remaining.remove(&node);
                }
            }
            current_set.sort_unstable();
            sets.push(current_set);
        }
        sets
    }
}

