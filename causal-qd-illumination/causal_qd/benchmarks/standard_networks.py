class InsuranceBenchmark(BenchmarkNetwork):
    """Insurance network: 27 nodes.

    A Bayesian network for evaluating automobile insurance risks.

    Due to its size, only the core structure is defined.
    """

    @property
    def name(self) -> str:
        return "insurance"

    @property
    def n_nodes(self) -> int:
        return 27

    @property
    def node_names(self) -> List[str]:
        return [
            "SocioEcon", "GoodStudent", "Age", "RiskAversion",
            "VehicleYear", "ThisCarDam", "RuggedAuto", "Accident",
            "MakeModel", "DrivQuality", "Mileage", "Antilock",
            "DrivingSkill", "SeniorTrain", "ThisCarCost", "Theft",
            "CarValue", "HomeBase", "AntiTheft", "PropCost",
            "OtherCarCost", "OtherCar", "MedCost", "Cushioning",
            "Airbag", "ILiCost", "DrivHist",
        ]

    @property
    def true_dag(self) -> AdjacencyMatrix:
        adj = np.zeros((27, 27), dtype=np.int8)
        edges = [
            (0, 1), (0, 3), (0, 21),      # SocioEcon
            (2, 0), (2, 3), (2, 12),       # Age -> SocioEcon, RiskAversion, DrivingSkill
            (2, 13),                       # Age -> SeniorTrain
            (3, 9), (3, 17), (3, 18),      # RiskAversion
            (4, 6), (4, 16), (4, 8),       # VehicleYear
            (8, 6), (8, 16),               # MakeModel -> RuggedAuto, CarValue
            (12, 9), (12, 26),             # DrivingSkill -> DrivQuality, DrivHist
            (13, 12),                       # SeniorTrain -> DrivingSkill
            (10, 7), (10, 14),             # Mileage -> Accident, ThisCarCost
            (9, 7),                         # DrivQuality -> Accident
            (11, 7),                        # Antilock -> Accident
            (7, 5), (7, 20), (7, 22),      # Accident
            (7, 25),                        # Accident -> ILiCost
            (6, 23), (6, 5),               # RuggedAuto -> Cushioning, ThisCarDam
            (5, 14),                        # ThisCarDam -> ThisCarCost
            (14, 19),                       # ThisCarCost -> PropCost
            (16, 14),                       # CarValue -> ThisCarCost
            (16, 15),                       # CarValue -> Theft
            (17, 15),                       # HomeBase -> Theft
            (18, 15),                       # AntiTheft -> Theft
            (15, 19),                       # Theft -> PropCost
            (24, 23),                       # Airbag -> Cushioning
            (23, 22), (23, 25),            # Cushioning -> MedCost, ILiCost
        ]
        for i, j in edges:
            if i < 27 and j < 27:
                adj[i, j] = 1
        return adj


