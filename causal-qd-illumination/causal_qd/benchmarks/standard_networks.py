# Sachs network (11 nodes)
# ---------------------------------------------------------------------------


class SachsBenchmark(BenchmarkNetwork):
    """Sachs protein signaling network: 11 nodes.

    Represents protein signaling pathways in human immune system cells
    (Sachs et al., 2005).

    Nodes: Raf, Mek, Plcg, PIP2, PIP3, Erk, Akt, PKA, PKC, P38, Jnk
    """

    @property
    def name(self) -> str:
        return "sachs"

    @property
    def n_nodes(self) -> int:
        return 11

    @property
    def node_names(self) -> List[str]:
        return [
            "Raf",
            "Mek",
            "Plcg",
            "PIP2",
            "PIP3",
            "Erk",
            "Akt",
            "PKA",
            "PKC",
            "P38",
            "Jnk",
        ]

    @property
    def true_dag(self) -> AdjacencyMatrix:
        # Sachs consensus network
        adj = np.zeros((11, 11), dtype=np.int8)
        # 0=Raf,1=Mek,2=Plcg,3=PIP2,4=PIP3,5=Erk,6=Akt,7=PKA,8=PKC,9=P38,10=Jnk
        edges = [
            (2, 3),   # Plcg -> PIP2
            (2, 4),   # Plcg -> PIP3
            (4, 3),   # PIP3 -> PIP2
            (7, 0),   # PKA -> Raf
            (7, 1),   # PKA -> Mek
            (7, 5),   # PKA -> Erk
            (7, 6),   # PKA -> Akt
            (7, 10),  # PKA -> Jnk
            (7, 9),   # PKA -> P38
            (8, 0),   # PKC -> Raf
            (8, 1),   # PKC -> Mek
            (8, 10),  # PKC -> Jnk
            (8, 9),   # PKC -> P38
            (8, 7),   # PKC -> PKA
            (0, 1),   # Raf -> Mek
            (1, 5),   # Mek -> Erk
            (5, 6),   # Erk -> Akt
        ]
        for i, j in edges:
            adj[i, j] = 1
        return adj


